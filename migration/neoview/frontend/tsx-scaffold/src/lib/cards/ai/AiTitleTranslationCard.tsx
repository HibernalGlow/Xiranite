import { Fragment } from "react"
import { Switch } from '$lib/components/ui/switch';
import { Label } from '$lib/components/ui/label';
import { aiTranslationStore } from '$lib/stores/ai/translationStore.js';
import { Languages, Info } from 'lucide-react';
import * as Tooltip from '$lib/components/ui/tooltip';

export function AiTitleTranslationCard() {
  let config = $state(aiTranslationStore.getConfig());

  let stats = $state(aiTranslationStore.getCacheStats());

  $effect(() => {
      const unsubscribe = aiTranslationStore.subscribe((state) => {
        config = state.config;
        stats = {
          size: state.cache.size,
          ...state.stats
        };
      });
      return unsubscribe;
    });

  function toggleEnabled() {
      aiTranslationStore.setEnabled(!config.enabled);
    }

  function toggleAutoTranslate() {
      aiTranslationStore.updateConfig({ autoTranslate: !config.autoTranslate });
    }
  return (
    <Fragment>

<div className="space-y-4">
	{/*  功能开关  */}
	<div className="flex items-center justify-between">
		<div className="flex items-center gap-2">
			<Languages className="text-muted-foreground h-4 w-4"></Languages>
			<Label>启用 AI 标题翻译</Label>
		</div>
		<Switch checked={config.enabled} onCheckedChange={toggleEnabled}></Switch>
	</div>

	{config.enabled ? (<Fragment>
		{/*  自动翻译开关  */}
		<div className="flex items-center justify-between">
			<div className="flex items-center gap-2">
				<Label className="text-sm">自动翻译无 EMM 翻译的标题</Label>
				<Tooltip.Root>
					<Tooltip.Trigger>
						<Info className="text-muted-foreground h-3 w-3"></Info>
					</Tooltip.Trigger>
					<Tooltip.Content>
						<p className="max-w-xs text-xs">
							当文件没有 EMM 数据库中的翻译标题时，自动使用 AI 翻译日文标题
						</p>
					</Tooltip.Content>
				</Tooltip.Root>
			</div>
			<Switch checked={config.autoTranslate} onCheckedChange={toggleAutoTranslate}></Switch>
		</div>

		{/*  当前服务状态  */}
		<div className="bg-muted/30 rounded-md border p-3 text-sm">
			<div className="flex items-center justify-between">
				<span className="text-muted-foreground">当前服务:</span>
				<span className="font-medium">
					{config.type === 'libretranslate' ? (<Fragment>
						LibreTranslate
					</Fragment>) : (<Fragment>{config.type === 'ollama' ? (<Fragment>
						Ollama ({config.ollamaModel})
					</Fragment>) : (<Fragment>
						未配置
					</Fragment>)}</Fragment>)}
				</span>
			</div>
			<div className="mt-1 flex items-center justify-between">
				<span className="text-muted-foreground">缓存数量:</span>
				<span className="font-medium">{stats.size}</span>
			</div>
			<div className="mt-1 flex items-center justify-between">
				<span className="text-muted-foreground">API 调用:</span>
				<span className="font-medium">{stats.apiCalls}</span>
			</div>
		</div>

		{config.type === 'disabled' ? (<Fragment>
			<p className="text-xs text-amber-600 dark:text-amber-400">请在"翻译服务配置"卡片中配置翻译服务</p>
		</Fragment>) : null}
	</Fragment>) : null}
</div></Fragment>
  )
}
