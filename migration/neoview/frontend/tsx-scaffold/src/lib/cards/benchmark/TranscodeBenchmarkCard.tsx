interface TranscodeResult {
		method: string;
		input_format: string;
		output_format: string;
		decode_ms: number;
		encode_ms: number;
		total_ms: number;
		input_size: number;
		output_size: number;
		image_size: [number, number] | null;
		success: boolean;
		error: string | null;
	}
interface TranscodeReport {
		file_path: string;
		file_size: number;
		detected_format: string;
		results: TranscodeResult[];
	}

