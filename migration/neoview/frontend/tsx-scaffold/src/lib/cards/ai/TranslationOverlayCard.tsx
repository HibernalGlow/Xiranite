import { Fragment } from "react"
import { Switch } from '$lib/components/ui/switch';
import { Label } from '$lib/components/ui/label';
import { Button } from '$lib/components/ui/button';
import { Slider } from '$lib/components/ui/slider';
import { Separator } from '$lib/components/ui/separator';
import { translationStore } from '$lib/stores/translation/translationStore.js';
import { showInfoToast, showSuccessToast, showErrorToast } from '$lib/utils/toast';
import { bookStore } from '$lib/stores/book.js';
import { Eye, EyeOff, Square, Type, Download, Upload, Trash2, Palette } from 'lucide-react';

export function TranslationOverlayCard() {
  let settings = $derived.by(() => {
      void translationStore.version;
      return translationStore.overlaySettings;
    });

  let currentPageRegionCount = $derived.by(() => {
      void translationStore.version;
      const pageIndex = bookStore.currentPageIndex;
      return translationStore.getRegions(pageIndex).length;
    });

  let totalRegionCount = $derived.by(() => {
      void translationStore.version;
      let count = 0;
      for (const regions of translationStore.getAllRegions().values()) {
        count += regions.length;
      }
      return count;
    });

  function handleToggleEnabled(checked: boolean) {
      translationStore.updateSettings({ enabled: checked });
      showInfoToast(checked ? '翻译叠加层已开启' : '翻译叠加层已关闭');
    }

  function handleToggleBoundingBox(checked: boolean) {
      translationStore.updateSettings({ showBoundingBox: checked });
    }

  function handleToggleShowOriginal(checked: boolean) {
      translationStore.updateSettings({ showOriginal: checked });
    }

  function handleToggleShowTranslation(checked: boolean) {
      translationStore.updateSettings({ showTranslation: checked });
    }

  function handleToggleHoverOriginal(checked: boolean) {
      translationStore.updateSettings({ showOriginalOnHover: checked });
    }

  function handleOpacityChange(value: number) {
      translationStore.updateSettings({ opacity: value });
    }

  async function handleExport() {
      try {
        const json = translationStore.exportToJson();
        const blob = new Blob([json], { type: 'application/json' });
        const url = URL.createObjectURL(blob);

        const a = document.createElement('a');
        a.href = url;
        a.download = `translation_regions_${Date.now()}.json`;
        a.click();

        URL.revokeObjectURL(url);
        showSuccessToast('导出成功');
      } catch (e) {
        showErrorToast('导出失败: ' + String(e));
      }
    }

  async function handleImport() {
      const input = document.createElement('input');
      input.type = 'file';
      input.accept = '.json';
      input.onchange = async (e) => {
        const file = (e.target as HTMLInputElement).files?.[0];
        if (!file) return;

        try {
          const text = await file.text();
          const result = translationStore.importFromJson(text);

          if (result.success) {
            showSuccessToast(`导入成功，共 ${result.count} 个区域`);
          } else {
            showErrorToast('导入失败: ' + result.error);
          }
        } catch (e) {
          showErrorToast('读取文件失败: ' + String(e));
        }
      };
      input.click();
    }

  function handleClearCurrentPage() {
      const pageIndex = bookStore.currentPageIndex;
      translationStore.clearPage(pageIndex);
      showInfoToast('已清除当前页翻译区域');
    }

  function handleClearAll() {
      if (confirm('确定要清除所有翻译区域吗？')) {
        translationStore.clearAll();
        showInfoToast('已清除所有翻译区域');
      }
    }

  function handleAddTestRegion() {
      const pageIndex = bookStore.currentPageIndex;
      const region = translationStore.createRegion(
        pageIndex,
        [
          { x: 100, y: 100 },
          { x: 300, y: 100 },
          { x: 300, y: 200 },
          { x: 100, y: 200 }
        ],
        'これはテストです',
        '这是一个测试'
      );
      translationStore.addRegion(region);
      showSuccessToast('已添加测试区域');
    }
  return (
    <Fragment>{/* 
  TranslationOverlayCard - 翻译叠加层控制卡片
  
  功能：
  - 开启/关闭翻译叠加层
  - 显示/隐藏边界框
  - 配置显示选项（原文、译文、悬停显示）
  - 导入/导出翻译数据
  - 清除翻译区域
 */}


<div className="space-y-3 text-xs">
	{/*  主开关  */}
	<div className="flex items-center justify-between">
		<Label className="flex items-center gap-1.5 text-xs font-medium">
			{settings.enabled ? (<Fragment>
				<Eye className="text-primary h-3.5 w-3.5"></Eye>
			</Fragment>) : (<Fragment>
				<EyeOff className="text-muted-foreground h-3.5 w-3.5"></EyeOff>
			</Fragment>)}
			翻译叠加层
		</Label>
		<Switch checked={settings.enabled} onCheckedChange={handleToggleEnabled} className="scale-90"></Switch>
	</div>
	<p className="text-muted-foreground -mt-1 text-[10px]">在图片上显示翻译区域和译文</p>

	<Separator></Separator>

	{/*  显示选项  */}
	<div className="space-y-2">
		<p className="text-muted-foreground text-[10px] font-medium tracking-wide uppercase">显示选项</p>

		<div className="flex items-center justify-between">
			<Label className="flex items-center gap-1.5 text-xs">
				<Square className="h-3 w-3"></Square>
				显示边界框
			</Label>
			<Switch checked={settings.showBoundingBox} onCheckedChange={handleToggleBoundingBox} className="scale-75" disabled={!settings.enabled}></Switch>
		</div>

		<div className="flex items-center justify-between">
			<Label className="flex items-center gap-1.5 text-xs">
				<Type className="h-3 w-3"></Type>
				显示译文
			</Label>
			<Switch checked={settings.showTranslation} onCheckedChange={handleToggleShowTranslation} className="scale-75" disabled={!settings.enabled}></Switch>
		</div>

		<div className="flex items-center justify-between">
			<Label className="text-xs">显示原文</Label>
			<Switch checked={settings.showOriginal} onCheckedChange={handleToggleShowOriginal} className="scale-75" disabled={!settings.enabled}></Switch>
		</div>

		<div className="flex items-center justify-between">
			<Label className="text-xs">悬停显示原文</Label>
			<Switch checked={settings.showOriginalOnHover} onCheckedChange={handleToggleHoverOriginal} className="scale-75" disabled={!settings.enabled}></Switch>
		</div>
	</div>

	<Separator></Separator>

	{/*  透明度  */}
	<div className="space-y-2">
		<div className="flex items-center justify-between">
			<Label className="flex items-center gap-1.5 text-xs">
				<Palette className="h-3 w-3"></Palette>
				透明度
			</Label>
			<span className="text-muted-foreground text-[10px]">{Math.round(settings.opacity * 100)}%</span>
		</div>
		<Slider type="single" value={settings.opacity} min={0} max={1} step={0.05} onValueChange={handleOpacityChange} disabled={!settings.enabled} className="w-full"></Slider>
	</div>

	<Separator></Separator>

	{/*  统计信息  */}
	<div className="bg-muted/50 space-y-1 rounded-md p-2">
		<div className="flex items-center justify-between text-[10px]">
			<span className="text-muted-foreground">当前页区域</span>
			<span className="font-mono">{currentPageRegionCount}</span>
		</div>
		<div className="flex items-center justify-between text-[10px]">
			<span className="text-muted-foreground">总区域数</span>
			<span className="font-mono">{totalRegionCount}</span>
		</div>
	</div>

	<Separator></Separator>

	{/*  操作按钮  */}
	<div className="space-y-2">
		<p className="text-muted-foreground text-[10px] font-medium tracking-wide uppercase">数据管理</p>

		<div className="grid grid-cols-2 gap-1.5">
			<Button variant="outline" size="sm" className="h-7 text-[10px]" onClick={handleImport}>
				<Upload className="mr-1 h-3 w-3"></Upload>
				导入
			</Button>
			<Button variant="outline" size="sm" className="h-7 text-[10px]" onClick={handleExport}>
				<Download className="mr-1 h-3 w-3"></Download>
				导出
			</Button>
		</div>

		<div className="grid grid-cols-2 gap-1.5">
			<Button variant="outline" size="sm" className="h-7 text-[10px]" onClick={handleClearCurrentPage} disabled={currentPageRegionCount === 0}>
				<Trash2 className="mr-1 h-3 w-3"></Trash2>
				清除当前页
			</Button>
			<Button variant="destructive" size="sm" className="h-7 text-[10px]" onClick={handleClearAll} disabled={totalRegionCount === 0}>
				<Trash2 className="mr-1 h-3 w-3"></Trash2>
				清除全部
			</Button>
		</div>

		{/*  开发测试按钮  */}
		<Button variant="secondary" size="sm" className="h-7 w-full text-[10px]" onClick={handleAddTestRegion}>
			添加测试区域
		</Button>
	</div>
</div></Fragment>
  )
}
