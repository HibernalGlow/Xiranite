import { Fragment } from "react"
import { Star, RefreshCcw, Download, Trash2 } from 'lucide-react';
import * as Button from '$lib/components/ui/button';
import * as Input from '$lib/components/ui/input';
import * as Tooltip from '$lib/components/ui/tooltip';
import { folderRatingStore } from '$lib/stores/emm/folderRating';
import { emmMetadataStore } from '$lib/stores/emmMetadata.js';
import { confirm } from '$lib/stores/confirmDialog.js';

export function FolderRatingsCard() {
  let folderRatingStats = $state<{ count: number; lastUpdated: string | null }>({
      count: 0,
      lastUpdated: null
    });

  let isUpdatingRatings = $state(false);

  let folderRatingPath = $state('');

  $effect(() => {
      const unsubscribe = folderRatingStore.subscribe((cache) => {
        const count = Object.keys(cache.ratings).length;
        const lastUpdated = cache.lastUpdated ? new Date(cache.lastUpdated).toLocaleString() : null;
        folderRatingStats = { count, lastUpdated };
      });
      return unsubscribe;
    });

  async function handleUpdateFolderRatings() {
      isUpdatingRatings = true;
      try {
        await emmMetadataStore.calculateFolderRatings();
      } finally {
        isUpdatingRatings = false;
      }
    }

  function handleExportFolderRatings() {
      const cache = folderRatingStore.exportCache();
      const json = JSON.stringify(cache, null, 2);
      const blob = new Blob([json], { type: 'application/json' });
      const url = URL.createObjectURL(blob);

      const a = document.createElement('a');
      a.href = url;
      a.download = `neoview-folder-ratings-${Date.now()}.json`;
      document.body.appendChild(a);
      a.click();
      document.body.removeChild(a);
      URL.revokeObjectURL(url);
    }

  async function handleClearFolderRatings() {
      const confirmed = await confirm({
        title: '确认清除',
        description: '确定要清除所有文件夹评分缓存吗？',
        confirmText: '清除',
        cancelText: '取消',
        variant: 'destructive'
      });
      if (confirmed) {
        folderRatingStore.clearCache();
      }
    }

  function handleCalculateForPath() {
      if (folderRatingPath.trim()) {
        folderRatingStore.calculateRatingsForPath(folderRatingPath.trim());
      }
    }
  return (
    <Fragment>

<div className="space-y-2 text-xs">
	<p className="text-muted-foreground">
		基于 EMM 数据库计算的文件夹平均评分缓存。
		{folderRatingStats.lastUpdated ? (<Fragment>
			<br />更新: {folderRatingStats.lastUpdated}
		</Fragment>) : null}
	</p>

	<div className="text-muted-foreground flex items-center gap-1.5 text-[10px]">
		<Star className="h-3 w-3 text-amber-500"></Star>
		<span>缓存数量: <strong className="text-foreground">{folderRatingStats.count}</strong></span>
	</div>

	<div className="flex flex-wrap gap-1.5">
		<Tooltip.Root>
			<Tooltip.Trigger>
				<Button.Root variant="outline" size="sm" className="h-7 gap-1 px-2 text-[10px]" onClick={handleUpdateFolderRatings} disabled={isUpdatingRatings}>
					<RefreshCcw className="h-3 w-3"></RefreshCcw>
					{isUpdatingRatings ? '更新中...' : '重算'}
				</Button.Root>
			</Tooltip.Trigger>
			<Tooltip.Content>
				<p>重新计算所有文件夹的平均评分</p>
			</Tooltip.Content>
		</Tooltip.Root>
		<Tooltip.Root>
			<Tooltip.Trigger>
				<Button.Root variant="outline" size="sm" className="h-7 gap-1 px-2 text-[10px]" onClick={handleExportFolderRatings} disabled={folderRatingStats.count === 0}>
					<Download className="h-3 w-3"></Download>
					导出
				</Button.Root>
			</Tooltip.Trigger>
			<Tooltip.Content>
				<p>导出评分缓存为 JSON 文件</p>
			</Tooltip.Content>
		</Tooltip.Root>
		<Tooltip.Root>
			<Tooltip.Trigger>
				<Button.Root variant="outline" size="sm" className="text-destructive hover:text-destructive h-7 gap-1 px-2 text-[10px]" onClick={handleClearFolderRatings} disabled={folderRatingStats.count === 0}>
					<Trash2 className="h-3 w-3"></Trash2>
					清除
				</Button.Root>
			</Tooltip.Trigger>
			<Tooltip.Content>
				<p>清除所有评分缓存</p>
			</Tooltip.Content>
		</Tooltip.Root>
	</div>

	{/*  按路径补充评分  */}
	<div className="flex items-center gap-1.5">
		<Input.Root type="text" placeholder="输入路径补充评分..." className="h-7 flex-1 text-[10px]"></Input.Root>
		<Tooltip.Root>
			<Tooltip.Trigger>
				<Button.Root variant="outline" size="sm" className="h-7 gap-1 px-2 text-[10px]" onClick={handleCalculateForPath} disabled={!folderRatingPath || isUpdatingRatings}>
					补充
				</Button.Root>
			</Tooltip.Trigger>
			<Tooltip.Content>
				<p>根据子文件夹评分补充该路径的评分</p>
			</Tooltip.Content>
		</Tooltip.Root>
	</div>
</div></Fragment>
  )
}
