import { Fragment } from "react"
import * as Switch from '$lib/components/ui/switch';
import { animatedVideoModeStore } from '$lib/stores/animatedVideoMode.js';

export function AnimatedVideoModeCard() {
  const enabled = $derived(animatedVideoModeStore.enabled);

  const ffmpegAvailable = $derived(animatedVideoModeStore.ffmpegAvailable);

  const ffmpegChecked = $derived(animatedVideoModeStore.ffmpegChecked);

  const ffmpegChecking = $derived(animatedVideoModeStore.ffmpegChecking);

  const lastError = $derived(animatedVideoModeStore.lastError);

  const keywords = $derived(animatedVideoModeStore.keywords);

  let keywordText = $state('');

  $effect(() => {
      keywordText = keywords.join(', ');
    });

  function handleToggle(checked: boolean) {
      animatedVideoModeStore.setEnabled(checked);
    }

  async function handleRefreshFfmpeg() {
      await animatedVideoModeStore.refreshFfmpegAvailability();
    }

  function handleKeywordInput(value: string) {
      keywordText = value;
      animatedVideoModeStore.setKeywordsFromText(value);
    }
  return (
    <Fragment>

<div className="space-y-3 text-xs">
	<div className="flex items-center justify-between gap-2">
		<div className="space-y-0.5">
			<p className="text-foreground font-medium">动图按视频模式播放</p>
			<p className="text-muted-foreground text-[11px]">GIF/APNG 将走视频播放器，可使用倍速与循环。</p>
		</div>
		<Switch.Root checked={enabled} onCheckedChange={handleToggle} className="scale-75"></Switch.Root>
	</div>

	<div className="border-border/50 bg-muted/20 rounded-md border p-2">
		<p className="text-muted-foreground mb-1 text-[11px]">关键词直判（优先于 WebP 探测）</p>
		<textarea className="border-border bg-background min-h-16 w-full rounded border px-2 py-1 text-[11px]" value={keywordText} onInput={(e) => handleKeywordInput((e.currentTarget as HTMLTextAreaElement).value)} placeholder="例如: [#dyna], [#anim], __gif"></textarea>
		<p className="text-muted-foreground mt-1 text-[10px]">
			使用逗号或换行分隔。命中后直接按动图处理，跳过额外检测。
		</p>
	</div>

	<div className="border-border/50 bg-muted/20 rounded-md border p-2">
		<div className="flex items-center justify-between gap-2">
			<span className="text-muted-foreground">FFmpeg</span>
			<span className={`rounded-full px-2 py-0.5 text-[10px] ${ffmpegChecking
					? 'bg-amber-500/15 text-amber-600'
					: ffmpegAvailable
						? 'bg-emerald-500/15 text-emerald-600'
						: ffmpegChecked
							? 'bg-destructive/15 text-destructive'
							: 'bg-muted text-muted-foreground'}`}>
				{ffmpegChecking ? (<Fragment>
					检测中
				</Fragment>) : (<Fragment>{ffmpegAvailable ? (<Fragment>
					可用
				</Fragment>) : (<Fragment>{ffmpegChecked ? (<Fragment>
					不可用
				</Fragment>) : (<Fragment>
					未检测
				</Fragment>)}</Fragment>)}</Fragment>)}
			</span>
		</div>

		{!ffmpegAvailable && ffmpegChecked ? (<Fragment>
			<p className="text-muted-foreground mt-1 text-[10px]">
				未检测到 FFmpeg，将自动使用前端解码播放动图。
			</p>
		</Fragment>) : null}

		{lastError ? (<Fragment>
			<p className="text-muted-foreground mt-1 text-[10px]">错误: {lastError}</p>
		</Fragment>) : null}

		<div className="mt-2">
			<button type="button" onClick={handleRefreshFfmpeg} disabled={ffmpegChecking} className="border-border bg-background hover:bg-accent rounded border px-2 py-1 text-[10px] transition-colors disabled:cursor-not-allowed disabled:opacity-50">
				{ffmpegChecking ? '检测中...' : '重新检测 FFmpeg'}
			</button>
		</div>
	</div>

	{enabled ? (<Fragment>
		<p className="text-muted-foreground text-[10px]">
			已启用后，当前页若是 GIF/APNG/动画 WebP（或命中关键词），会自动进入视频模式。
		</p>
	</Fragment>) : null}
</div></Fragment>
  )
}
