import { Fragment } from "react"
import { ChevronDown, ChevronUp, Settings2 } from 'lucide-react';
import { Button } from '$lib/components/ui/button';
import UpscalePanelConditionTabs from './UpscalePanelConditionTabs.js';
import { Switch } from '$lib/components/ui/switch';
import { Label } from '$lib/components/ui/label';
import {
		conditionalUpscaleEnabled,
		autoUpscaleEnabled,
		conditionsList,
		availableModels,
		MODEL_LABELS,
		GPU_OPTIONS,
		TILE_SIZE_OPTIONS,
		NOISE_LEVEL_OPTIONS
	} from '$lib/stores/upscale/upscalePanelStore.js';
import { syncUpscaleConditions } from '$lib/services/upscaleConditionSync';

export function UpscaleConditionsCard() {
  let expanded = $state(false);

  const isAutoUpscaleEnabled = $derived(autoUpscaleEnabled.value);

  async function handleEnabledChange(checked: boolean) {
      conditionalUpscaleEnabled.value = checked;
      await syncUpscaleConditions(conditionalUpscaleEnabled.value, conditionsList.value);
    }

  async function handleConditionsChange(event: CustomEvent) {
      // 条件列表已通过 bindable 更新，统一同步
      await syncUpscaleConditions(conditionalUpscaleEnabled.value, conditionsList.value);
    }
  return (
    <Fragment>

<div className="space-y-3 text-xs">
	{/*  条件超分开关  */}
	<div className="flex items-center justify-between">
		<Label className="text-xs font-medium">条件超分</Label>
		<Switch checked={conditionalUpscaleEnabled.value} onCheckedChange={handleEnabledChange} className="scale-90" disabled={!isAutoUpscaleEnabled}></Switch>
	</div>
	<p className="text-muted-foreground -mt-1 text-[10px]">根据图片尺寸等条件决定是否超分</p>

	{!isAutoUpscaleEnabled ? (<Fragment>
		<div className="rounded bg-amber-500/10 p-2 text-[10px] text-amber-500">
			⚠️ 需要先启用「自动超分」才能生效
		</div>
	</Fragment>) : null}

	{/*  摘要信息  */}
	{conditionalUpscaleEnabled.value && isAutoUpscaleEnabled ? (<Fragment>
		<div className="flex items-center justify-between">
			<div className="flex items-center gap-2">
				<span className="text-muted-foreground">条件数量:</span>
				<span className="font-medium">{conditionsList.value.length}</span>
			</div>
			<div className="flex items-center gap-2">
				<span className="text-muted-foreground">状态:</span>
				<span className="text-green-500">已启用</span>
			</div>
		</div>

		{/*  启用的条件列表预览  */}
		{conditionsList.value.length > 0 ? (<Fragment>
			<div className="flex flex-wrap gap-1">
				{conditionsList.value.filter((c) => c.enabled).slice(0, 3).map((cond) => (<Fragment>
					<span className="bg-primary/10 text-primary rounded px-1.5 py-0.5 text-[10px]">
						{cond.name}
					</span>
				</Fragment>))}
				{conditionsList.value.filter((c) => c.enabled).length > 3 ? (<Fragment>
					<span className="text-muted-foreground text-[10px]">
						+{conditionsList.value.filter((c) => c.enabled).length - 3} 更多
					</span>
				</Fragment>) : null}
			</div>
		</Fragment>) : null}
	</Fragment>) : null}

	{/*  展开/折叠按钮  */}
	<Button variant="outline" size="sm" className="h-7 w-full text-xs" onClick={() => (expanded = !expanded)}>
		<Settings2 className="mr-1 h-3 w-3"></Settings2>
		{expanded ? '收起条件编辑器' : '展开条件编辑器'}
		{expanded ? (<Fragment>
			<ChevronUp className="ml-auto h-3 w-3"></ChevronUp>
		</Fragment>) : (<Fragment>
			<ChevronDown className="ml-auto h-3 w-3"></ChevronDown>
		</Fragment>)}
	</Button>

	{/*  条件编辑器  */}
	{expanded ? (<Fragment>
		<div className="mt-2 border-t pt-2">
			<UpscalePanelConditionTabs availableModels={availableModels.value} modelLabels={MODEL_LABELS} gpuOptions={GPU_OPTIONS} tileSizeOptions={TILE_SIZE_OPTIONS} noiseLevelOptions={NOISE_LEVEL_OPTIONS} onConditionsChanged={handleConditionsChange}></UpscalePanelConditionTabs>
		</div>
	</Fragment>) : null}
</div></Fragment>
  )
}
