import { Fragment } from "react"
import { infoPanelStore, type ViewerImageInfo } from '$lib/stores/infoPanel.js';

export function StorageCard() {
  let imageInfo = $state<ViewerImageInfo | null>(null);

  $effect(() => {
      const unsubscribe = infoPanelStore.subscribe((state) => {
        imageInfo = state.imageInfo;
      });
      return unsubscribe;
    });

  function formatFileSize(bytes?: number): string {
      if (bytes === undefined) return '—';
      if (bytes < 1024) return `${bytes} B`;
      if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(2)} KB`;
      if (bytes < 1024 * 1024 * 1024) return `${(bytes / (1024 * 1024)).toFixed(2)} MB`;
      return `${(bytes / (1024 * 1024 * 1024)).toFixed(2)} GB`;
    }
  return (
    <Fragment>

{imageInfo ? (<Fragment>
	<div className="space-y-2 text-sm">
		<div className="flex justify-between">
			<span className="text-muted-foreground">路径:</span>
			<span className="max-w-[200px] font-mono text-xs break-words" title={imageInfo.path}>
				{imageInfo.path ?? '—'}
			</span>
		</div>
		<div className="flex justify-between">
			<span className="text-muted-foreground">大小:</span>
			<span>{formatFileSize(imageInfo.fileSize)}</span>
		</div>
	</div>
</Fragment>) : (<Fragment>
	<div className="text-muted-foreground py-2 text-center text-sm">暂无存储信息</div>
</Fragment>)}</Fragment>
  )
}
