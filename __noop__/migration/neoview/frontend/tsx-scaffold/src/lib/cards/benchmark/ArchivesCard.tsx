import { Fragment } from "react"
import { invoke } from '@tauri-apps/api/core';
import { open } from '@tauri-apps/plugin-dialog';
import { Button } from '$lib/components/ui/button';
import { FolderOpen, Play, Trash2 } from 'lucide-react';
interface ArchiveScanResult {
		total_count: number;
		folder_path: string;
	}
interface BenchmarkReport {
		file_path: string;
		file_size: number;
		results: Array<{
			method: string;
			format: string;
			duration_ms: number;
			success: boolean;
			error: string | null;
		}>;
	}

export function ArchivesCard() {
  let selectedArchiveFolder = $state<string>('');

  let archiveScanResult = $state<ArchiveScanResult | null>(null);

  let archiveTier = $state<20 | 50 | 100 | 300>(20);

  let isScanning = $state(false);

  let isRunning = $state(false);

  let reports = $state<BenchmarkReport[]>([]);

  async function selectArchiveFolder() {
      const folder = await open({
        directory: true,
        multiple: false
      });

      if (folder && typeof folder === 'string') {
        selectedArchiveFolder = folder;
        isScanning = true;

        try {
          const result = await invoke<ArchiveScanResult>('scan_archives_in_folder', {
            folderPath: folder
          });
          archiveScanResult = result;
        } catch (e) {
          console.error('扫描压缩包失败:', e);
          archiveScanResult = null;
        } finally {
          isScanning = false;
        }
      }
    }

  async function runArchiveBenchmark() {
      if (!selectedArchiveFolder) return;

      isRunning = true;
      reports = [];

      try {
        const results = await invoke<BenchmarkReport[]>('run_archive_folder_benchmark', {
          folderPath: selectedArchiveFolder,
          tier: archiveTier
        });
        reports = results;
      } catch (err) {
        console.error('压缩包批量测试失败:', err);
      } finally {
        isRunning = false;
      }
    }

  function clearArchives() {
      selectedArchiveFolder = '';
      archiveScanResult = null;
      reports = [];
    }
  return (
    <Fragment>

<div className="space-y-2">
	<div className="flex gap-2">
		<Button onClick={selectArchiveFolder} variant="outline" size="sm" className="flex-1 text-xs" disabled={isScanning}>
			<FolderOpen className="mr-1 h-3 w-3"></FolderOpen>
			{isScanning ? '扫描中...' : selectedArchiveFolder ? '重选文件夹' : '选择文件夹'}
		</Button>
		<Button onClick={runArchiveBenchmark} disabled={isRunning || !archiveScanResult || archiveScanResult.total_count === 0} size="sm" className="flex-1 text-xs">
			<Play className="mr-1 h-3 w-3"></Play>
			{isRunning ? '测试中...' : '开始测试'}
		</Button>
		{selectedArchiveFolder ? (<Fragment>
			<Button onClick={clearArchives} variant="ghost" size="sm" className="text-xs">
				<Trash2 className="h-3 w-3"></Trash2>
			</Button>
		</Fragment>) : null}
	</div>

	{/*  扫描结果显示  */}
	{archiveScanResult ? (<Fragment>
		<div className="bg-muted/50 space-y-1 rounded p-2 text-[10px]">
			<div className="flex justify-between">
				<span className="text-muted-foreground">找到压缩包:</span>
				<span className="text-primary font-medium">{archiveScanResult.total_count} 个</span>
			</div>
			<div className="text-muted-foreground truncate" title={archiveScanResult.folder_path}>
				📁 {archiveScanResult.folder_path}
			</div>
		</div>
	</Fragment>) : null}

	{/*  档位选择  */}
	{archiveScanResult && archiveScanResult.total_count > 0 ? (<Fragment>
		<div className="flex items-center gap-1 text-[10px]">
			<span className="text-muted-foreground mr-1">抽样数:</span>
			{[20, 50, 100, 300].map((tier) => (<Fragment>
				<button type="button" className={`rounded px-2 py-0.5 ${archiveTier === tier
						? 'bg-primary text-primary-foreground'
						: 'bg-muted hover:bg-muted/80'}`} onClick={() => (archiveTier = tier as 20 | 50 | 100 | 300)} disabled={tier > archiveScanResult.total_count}>
					{Math.min(tier, archiveScanResult.total_count)}
				</button>
			</Fragment>))}
		</div>
		<div className="text-muted-foreground text-[10px]">
			将随机抽取 {Math.min(archiveTier, archiveScanResult.total_count)} 个压缩包测试
		</div>
	</Fragment>) : null}
</div></Fragment>
  )
}
