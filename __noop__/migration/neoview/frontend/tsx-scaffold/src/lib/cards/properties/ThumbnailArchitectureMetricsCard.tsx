import { Fragment } from "react"
import { Button } from '$lib/components/ui/button';
import { RefreshCw } from 'lucide-react';
import { unifiedThumbnailStore } from '$lib/stores/unifiedThumbnailStore.js';
interface SimpleStats {
		cached: number;
		loading: number;
		total: number;
	}

export function ThumbnailArchitectureMetricsCard() {
  let stats = $state<SimpleStats | null>(null);

  let isLoading = $state(false);

  let lastUpdated = $state<number>(0);

  async function refreshStats() {
      isLoading = true;
      try {
        stats = unifiedThumbnailStore.getStats();
        lastUpdated = Date.now();
      } finally {
        isLoading = false;
      }
    }

  onMount(() => {
      refreshStats();
      const timer = setInterval(refreshStats, 2000);
      return () => clearInterval(timer);
    });
  return (
    <Fragment>

<div className="space-y-3 text-xs">
	<div className="flex items-center justify-between">
		<div className="text-muted-foreground text-[11px]">
			{lastUpdated > 0 ? (<Fragment>
				更新于 {new Date(lastUpdated).toLocaleTimeString()}
			</Fragment>) : (<Fragment>
				暂无数据
			</Fragment>)}
		</div>
		<Button variant="outline" size="sm" onClick={refreshStats} disabled={isLoading}>
			<RefreshCw className={`mr-1 h-3 w-3 ${isLoading ? 'animate-spin' : ''}`}></RefreshCw>
			刷新
		</Button>
	</div>

	{stats ? (<Fragment>
		<div className="grid grid-cols-2 gap-2">
			<div className="rounded border p-2">
				<div className="text-muted-foreground">已缓存</div>
				<div className="font-mono text-sm">{stats.cached}</div>
			</div>
			<div className="rounded border p-2">
				<div className="text-muted-foreground">加载中</div>
				<div className="font-mono text-sm">{stats.loading}</div>
			</div>
			<div className="rounded border p-2">
				<div className="text-muted-foreground">总条目</div>
				<div className="font-mono text-sm">{stats.total}</div>
			</div>
		</div>
	</Fragment>) : (<Fragment>
		<div className="text-muted-foreground rounded border p-3">未获取到缩略图架构指标</div>
	</Fragment>)}
</div></Fragment>
  )
}
