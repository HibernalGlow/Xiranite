import { Fragment } from "react"
import { invoke } from '@tauri-apps/api/core';
import { open } from '@tauri-apps/plugin-dialog';
import { Button } from '$lib/components/ui/button';
import { FolderOpen, Play, Trash2 } from 'lucide-react';
interface DetailedBenchmarkResult {
		method: string;
		format: string;
		extract_ms: number;
		decode_ms: number;
		scale_ms: number;
		encode_ms: number;
		total_ms: number;
		success: boolean;
		error: string | null;
		input_size: number;
		output_size: number | null;
		original_dims: [number, number] | null;
		output_dims: [number, number] | null;
	}

export function DetailedCard() {
  let selectedDetailedFile = $state<string>('');

  let detailedResults = $state<DetailedBenchmarkResult[]>([]);

  let isRunning = $state(false);

  async function selectDetailedFile() {
      const file = await open({
        multiple: false,
        filters: [{ name: '压缩包', extensions: ['zip', 'cbz', 'rar', '7z', 'cb7', 'cbr'] }]
      });

      if (file && typeof file === 'string') {
        selectedDetailedFile = file;
      }
    }

  async function runDetailedBenchmark() {
      if (!selectedDetailedFile) return;
      isRunning = true;
      detailedResults = [];

      try {
        const results = await invoke<DetailedBenchmarkResult[]>('run_detailed_benchmark', {
          archivePath: selectedDetailedFile
        });
        detailedResults = results;
      } catch (e) {
        console.error('详细测试失败:', e);
      }

      isRunning = false;
    }

  function clearDetailedFile() {
      selectedDetailedFile = '';
      detailedResults = [];
    }

  function formatFileSize(bytes: number): string {
      if (bytes < 1024) return `${bytes} B`;
      if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
      return `${(bytes / (1024 * 1024)).toFixed(2)} MB`;
    }
  return (
    <Fragment>

<div className="space-y-2">
	<p className="text-muted-foreground text-[10px]">对比不同解码方法在各步骤的性能表现</p>
	<div className="flex gap-2">
		<Button onClick={selectDetailedFile} variant="outline" size="sm" className="flex-1 text-xs">
			<FolderOpen className="mr-1 h-3 w-3"></FolderOpen>
			{selectedDetailedFile ? '已选择' : '选择压缩包'}
		</Button>
		<Button onClick={runDetailedBenchmark} disabled={isRunning || !selectedDetailedFile} size="sm" className="flex-1 text-xs">
			<Play className="mr-1 h-3 w-3"></Play>
			{isRunning ? '测试中...' : '详细对比'}
		</Button>
		{selectedDetailedFile ? (<Fragment>
			<Button onClick={clearDetailedFile} variant="ghost" size="sm" className="text-xs">
				<Trash2 className="h-3 w-3"></Trash2>
			</Button>
		</Fragment>) : null}
	</div>

	{selectedDetailedFile ? (<Fragment>
		<div className="text-muted-foreground truncate text-[10px]">
			{selectedDetailedFile.split(/[/\\]/).pop()}
		</div>
	</Fragment>) : null}

	{detailedResults.length > 0 ? (<Fragment>
		<div className="max-h-60 space-y-2 overflow-auto text-[10px]">
			{detailedResults.map((result) => (<Fragment>
				<div className="space-y-1 rounded border p-2">
					<div className="flex justify-between font-medium">
						<span>
							{result.method}
						</span>
						<span className={`font-mono ${result.success ? 'text-green-600' : 'text-red-500'}`}>
							{result.success ? `${result.total_ms.toFixed(1)}ms` : '失败'}
						</span>
					</div>
					{result.success ? (<Fragment>
						<div className="text-muted-foreground grid grid-cols-2 gap-x-4 gap-y-0.5">
							<div>提取: <span className="font-mono">{result.extract_ms.toFixed(1)}ms</span></div>
							<div>解码: <span className="font-mono">{result.decode_ms.toFixed(1)}ms</span></div>
							<div>缩放: <span className="font-mono">{result.scale_ms.toFixed(1)}ms</span></div>
							<div>编码: <span className="font-mono">{result.encode_ms.toFixed(1)}ms</span></div>
							{result.original_dims ? (<Fragment>
								<div>
									原尺寸: <span className="font-mono">{result.original_dims[0]}×{result.original_dims[1]}</span>
								</div>
							</Fragment>) : null}
							{result.output_dims ? (<Fragment>
								<div>
									输出: <span className="font-mono">{result.output_dims[0]}×{result.output_dims[1]}</span>
								</div>
							</Fragment>) : null}
						</div>
					</Fragment>) : (<Fragment>{result.error ? (<Fragment>
						<div className="text-[9px] text-red-500">{result.error}</div>
					</Fragment>) : null}</Fragment>)}
				</div>
			</Fragment>))}
		</div>
	</Fragment>) : null}
</div></Fragment>
  )
}
