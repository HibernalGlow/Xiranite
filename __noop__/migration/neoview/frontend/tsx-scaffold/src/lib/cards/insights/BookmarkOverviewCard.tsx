import { Fragment } from "react"
import { bookmarkStore } from '$lib/stores/bookmark.js';
import { Badge } from '$lib/components/ui/badge';
import { Progress } from '$lib/components/ui/progress';

export function BookmarkOverviewCard() {
  let bookmarks = $state<ReturnType<typeof bookmarkStore.getAll>>([]);

  $effect(() => {
      const unsubscribe = bookmarkStore.subscribe((value) => {
        bookmarks = value ?? [];
      });
      return unsubscribe;
    });

  function buildBookmarkStats() {
      const total = bookmarks.length;
      const byType: Record<string, number> = {};
      for (const bm of bookmarks) {
        const type = bm.type ?? 'unknown';
        byType[type] = (byType[type] ?? 0) + 1;
      }
      const topTypes = Object.entries(byType)
        .sort((a, b) => b[1] - a[1])
        .slice(0, 3);
      return { total, topTypes };
    }

  const stats = $derived(buildBookmarkStats());
  return (
    <Fragment>

<div className="space-y-3">
	<div className="flex items-center justify-between">
		<span className="text-muted-foreground text-xs">总计书签</span>
		<span className="text-lg font-semibold">{stats.total}</span>
	</div>
	{stats.topTypes.length > 0 ? (<Fragment>
		<div className="space-y-2">
			{stats.topTypes.map(([type, count]) => (<Fragment>
				<div className="flex items-center gap-2">
					<Badge variant="outline" className="text-[10px]">{type}</Badge>
					<Progress value={(count / stats.total) * 100} className="h-1.5 flex-1"></Progress>
					<span className="text-muted-foreground text-xs">{count}</span>
				</div>
			</Fragment>))}
		</div>
	</Fragment>) : (<Fragment>
		<p className="text-muted-foreground py-4 text-center text-xs">暂无书签数据</p>
	</Fragment>)}
</div></Fragment>
  )
}
