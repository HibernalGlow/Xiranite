import { Fragment } from "react"
import { invoke } from '@tauri-apps/api/core';
import { open } from '@tauri-apps/plugin-dialog';
import { Button } from '$lib/components/ui/button';
import { FolderOpen, Play, Trash2 } from 'lucide-react';
interface BenchmarkReport {
		file_path: string;
		file_size: number;
		results: Array<{
			method: string;
			format: string;
			duration_ms: number;
			success: boolean;
			error: string | null;
			image_size: [number, number] | null;
			output_size: number | null;
		}>;
	}

export function FilesCard() {
  let selectedFiles = $state<string[]>([]);

  let reports = $state<BenchmarkReport[]>([]);

  let isRunning = $state(false);

  async function selectFiles() {
      const files = await open({
        multiple: true,
        filters: [
          {
            name: '图像文件',
            extensions: ['jpg', 'jpeg', 'png', 'gif', 'webp', 'bmp', 'avif', 'jxl', 'heic', 'heif']
          }
        ]
      });

      if (files) {
        selectedFiles = Array.isArray(files) ? files : [files];
      }
    }

  async function runBenchmark() {
      if (selectedFiles.length === 0) return;

      isRunning = true;
      reports = [];

      try {
        const results = await invoke<BenchmarkReport[]>('run_batch_benchmark', {
          filePaths: selectedFiles
        });
        reports = results;
      } catch (err) {
        console.error('基准测试失败:', err);
      } finally {
        isRunning = false;
      }
    }

  function clearFiles() {
      selectedFiles = [];
      reports = [];
    }

  function formatFileSize(bytes: number): string {
      if (bytes < 1024) return `${bytes} B`;
      if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
      return `${(bytes / (1024 * 1024)).toFixed(2)} MB`;
    }
  return (
    <Fragment>

<div className="space-y-2">
	<div className="flex gap-2">
		<Button onClick={selectFiles} variant="outline" size="sm" className="flex-1 text-xs">
			<FolderOpen className="mr-1 h-3 w-3"></FolderOpen>
			选择图像 ({selectedFiles.length})
		</Button>
		<Button onClick={runBenchmark} disabled={isRunning || selectedFiles.length === 0} size="sm" className="flex-1 text-xs">
			<Play className="mr-1 h-3 w-3"></Play>
			{isRunning ? '测试中...' : '开始测试'}
		</Button>
		{selectedFiles.length > 0 ? (<Fragment>
			<Button onClick={clearFiles} variant="ghost" size="sm" className="text-xs">
				<Trash2 className="h-3 w-3"></Trash2>
			</Button>
		</Fragment>) : null}
	</div>

	{selectedFiles.length > 0 ? (<Fragment>
		<div className="text-muted-foreground max-h-16 space-y-0.5 overflow-auto text-[10px]">
			{selectedFiles.map((file) => (<Fragment>
				<div className="truncate">{file.split(/[/\\]/).pop()}</div>
			</Fragment>))}
		</div>
	</Fragment>) : null}

	{/*  测试结果  */}
	{reports.length > 0 ? (<Fragment>
		<div className="mt-2 space-y-2 border-t pt-2 text-[10px]">
			<div className="font-medium">测试结果</div>
			{reports.map((report) => (<Fragment>
				<div className="space-y-1 rounded border p-2">
					<div className="flex justify-between font-medium">
						<span className="truncate" title={report.file_path}>
							{report.file_path.split(/[/\\]/).pop()}
						</span>
						<span className="text-muted-foreground">{formatFileSize(report.file_size)}</span>
					</div>
					{report.results.map((result) => (<Fragment>
						<div className="text-muted-foreground flex items-center justify-between">
							<span>
								{result.method}
							</span>
							<span className="flex gap-2">
								{result.output_size ? (<Fragment>
									<span>{formatFileSize(result.output_size)}</span>
								</Fragment>) : null}
								{result.success ? (<Fragment>
									<span className="font-mono text-green-600">{result.duration_ms.toFixed(1)}ms</span>
								</Fragment>) : (<Fragment>
									<span className="text-red-500">失败</span>
								</Fragment>)}
							</span>
						</div>
					</Fragment>))}
				</div>
			</Fragment>))}
		</div>
	</Fragment>) : null}
</div></Fragment>
  )
}
