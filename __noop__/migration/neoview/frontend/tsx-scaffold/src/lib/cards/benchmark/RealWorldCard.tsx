import { Fragment } from "react"
import { invoke } from '@tauri-apps/api/core';
import { open } from '@tauri-apps/plugin-dialog';
import { Button } from '$lib/components/ui/button';
import { FolderOpen, Play } from 'lucide-react';
interface RealWorldTestResult {
		viewport_size: number;
		total_files: number;
		total_time_ms: number;
		avg_time_ms: number;
		cached_count: number;
		generated_count: number;
		failed_count: number;
		throughput: number;
	}

export function RealWorldCard() {
  let selectedRealWorldFolder = $state<string>('');

  let realWorldResult = $state<RealWorldTestResult | null>(null);

  let viewportSize = $state<number>(20);

  let isRunning = $state(false);

  async function selectRealWorldFolder() {
      const folder = await open({
        multiple: false,
        directory: true
      });

      if (folder && typeof folder === 'string') {
        selectedRealWorldFolder = folder;
      }
    }

  async function runRealWorldTest() {
      if (!selectedRealWorldFolder) return;

      isRunning = true;
      realWorldResult = null;

      try {
        const result = await invoke<RealWorldTestResult>('run_realworld_benchmark', {
          folderPath: selectedRealWorldFolder,
          viewportSize: viewportSize
        });
        realWorldResult = result;
      } catch (err) {
        console.error('真实场景测试失败:', err);
      } finally {
        isRunning = false;
      }
    }
  return (
    <Fragment>

<div className="space-y-2">
	<p className="text-muted-foreground text-[10px]">模拟虚拟列表的可见区域，测试缩略图加载性能</p>
	<div className="flex gap-2">
		<Button onClick={selectRealWorldFolder} variant="outline" size="sm" className="flex-1 text-xs">
			<FolderOpen className="mr-1 h-3 w-3"></FolderOpen>
			{selectedRealWorldFolder ? '重选' : '选择文件夹'}
		</Button>
		<Button onClick={runRealWorldTest} disabled={isRunning || !selectedRealWorldFolder} size="sm" className="flex-1 text-xs">
			<Play className="mr-1 h-3 w-3"></Play>
			{isRunning ? '测试中...' : '开始测试'}
		</Button>
	</div>
	{selectedRealWorldFolder ? (<Fragment>
		<div className="text-muted-foreground truncate text-[10px]" title={selectedRealWorldFolder}>
			📁 {selectedRealWorldFolder}
		</div>
	</Fragment>) : null}
	<div className="flex items-center gap-2">
		<span className="text-muted-foreground text-[10px]">可见区域大小:</span>
		<div className="flex gap-1">
			{[10, 20, 50, 100].map((size) => (<Fragment>
				<button type="button" className={`rounded px-2 py-0.5 text-[10px] ${viewportSize === size
						? 'bg-primary text-primary-foreground'
						: 'bg-muted hover:bg-muted/80'}`} onClick={() => (viewportSize = size)}>
					{size}
				</button>
			</Fragment>))}
		</div>
	</div>
	{realWorldResult ? (<Fragment>
		<div className="space-y-1 rounded border p-2 text-[10px]">
			<div className="grid grid-cols-2 gap-x-4 gap-y-1">
				<div>文件数: <span className="font-mono">{realWorldResult.total_files}</span></div>
				<div>可见区域: <span className="font-mono">{realWorldResult.viewport_size}</span></div>
				<div>
					总用时: <span className="font-mono text-blue-500">{realWorldResult.total_time_ms.toFixed(0)}ms</span>
				</div>
				<div>
					平均: <span className="font-mono text-green-500">{realWorldResult.avg_time_ms.toFixed(1)}ms</span>
				</div>
				<div>
					缓存命中: <span className="font-mono text-purple-500">{realWorldResult.cached_count}</span>
				</div>
				<div>
					新生成: <span className="font-mono text-orange-500">{realWorldResult.generated_count}</span>
				</div>
				<div>失败: <span className="font-mono text-red-500">{realWorldResult.failed_count}</span></div>
				<div>
					吞吐量: <span className="font-mono text-cyan-500">{realWorldResult.throughput.toFixed(1)}/s</span>
				</div>
			</div>
		</div>
	</Fragment>) : null}
</div></Fragment>
  )
}
