import { Fragment } from "react"
import { pipelineLatencyStore } from '$lib/stores/pipelineLatency.js';
import { Activity, RefreshCw } from 'lucide-react';
import { Button } from '$lib/components/ui/button';

export function PipelineLatencyCard() {
  let records = $derived(pipelineLatencyStore.records);

  let stats = $derived(pipelineLatencyStore.getStats());

  let isMonitoring = $derived(pipelineLatencyStore.enabled);

  function toggleMonitoring() {
      pipelineLatencyStore.setEnabled(!pipelineLatencyStore.enabled);
    }

  function clearRecords() {
      pipelineLatencyStore.clear();
    }

  function formatSize(bytes: number): string {
      if (bytes < 1024) return `${bytes} B`;
      if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
      return `${(bytes / 1024 / 1024).toFixed(1)} MB`;
    }

  function formatMs(ms: number): string {
      if (ms < 1) return '<1ms';
      if (ms < 1000) return `${Math.round(ms)}ms`;
      return `${(ms / 1000).toFixed(2)}s`;
    }
  return (
    <Fragment>

<div className="space-y-3">
	{/*  控制按钮  */}
	<div className="flex gap-2">
		<Button variant={isMonitoring ? 'destructive' : 'default'} size="sm" onClick={toggleMonitoring}>
			<Activity className="mr-1 h-4 w-4"></Activity>
			{isMonitoring ? '停止监控' : '开始监控'}
		</Button>
		<Button variant="outline" size="sm" onClick={clearRecords}>
			<RefreshCw className="mr-1 h-4 w-4"></RefreshCw>
			清空
		</Button>
	</div>

	{/*  统计摘要  */}
	{stats.count > 0 ? (<Fragment>
		<div className="grid grid-cols-3 gap-2 text-sm">
			<div className="bg-muted/50 rounded p-2 text-center">
				<div className="text-muted-foreground text-xs">平均后端</div>
				<div className="font-mono font-bold">{formatMs(stats.avgBackendMs)}</div>
			</div>
			<div className="bg-muted/50 rounded p-2 text-center">
				<div className="text-muted-foreground text-xs">平均总耗时</div>
				<div className="font-mono font-bold">{formatMs(stats.avgTotalMs)}</div>
			</div>
			<div className="bg-muted/50 rounded p-2 text-center">
				<div className="text-muted-foreground text-xs">采样数</div>
				<div className="font-mono font-bold">{stats.count}</div>
			</div>
		</div>
	</Fragment>) : null}

	{/*  延迟记录列表  */}
	<div className="max-h-64 overflow-auto">
		{records.length === 0 ? (<Fragment>
			<div className="text-muted-foreground py-4 text-center">
				{isMonitoring ? '翻页后显示延迟数据' : '点击"开始监控"后翻页'}
			</div>
		</Fragment>) : (<Fragment>
			{/*  表头  */}
			<div className="text-muted-foreground mb-1 flex items-center gap-2 border-b px-2 py-1 text-[10px]">
				<span className="w-6">页</span>
				<span className="flex-1">IPC + Blob = 总</span>
				<span>大小</span>
			</div>
			<div className="space-y-1">
				{records.toReversed().map((record) => (<Fragment key={record.timestamp}>
					<div className={`flex items-center gap-2 rounded px-2 py-1 text-xs ${record.isCurrentPage
							? 'bg-primary/20'
							: 'bg-muted/30'}`}>
						<span className={`w-6 ${record.isCurrentPage
								? 'text-primary font-medium'
								: 'text-muted-foreground'}`}>
							P{record.pageIndex}
						</span>
						<span className="flex-1 font-mono text-[11px]">
							{/*  IPC 时间  */}
							<span title="IPC传输">
								{formatMs(record.ipcTransferMs)}
							</span>
							<span className="text-muted-foreground mx-0.5">+</span>
							{/*  Blob 创建时间  */}
							<span title="Blob创建">
								{formatMs(record.blobCreateMs)}
							</span>
							<span className="text-muted-foreground mx-0.5">=</span>
							{/*  总时间  */}
							<span title="总耗时">{formatMs(record.totalMs)}</span>
						</span>
						<span className="text-muted-foreground text-[10px]">{formatSize(record.dataSize)}</span>
						{/*  槽位标识  */}
						{record.slot ? (<Fragment>
							<span className={`rounded px-1 text-[10px] ${record.slot === 'current'
									? 'bg-green-500/20 text-green-500'
									: record.slot === 'next'
										? 'bg-blue-500/20 text-blue-500'
										: 'bg-orange-500/20 text-orange-500'}`}>
								{record.slot === 'current' ? '当' : record.slot === 'next' ? '后' : '前'}
							</span>
						</Fragment>) : null}
						{/*  状态标识  */}
						{record.cacheHit ? (<Fragment>
							<span className="text-[10px] font-medium text-green-500">缓存</span>
						</Fragment>) : (<Fragment>{record.source === 'preload' ? (<Fragment>
							<span className="text-[10px] text-blue-500">预</span>
						</Fragment>) : null}</Fragment>)}
					</div>
				</Fragment>))}
			</div>
		</Fragment>)}
	</div>
</div></Fragment>
  )
}
