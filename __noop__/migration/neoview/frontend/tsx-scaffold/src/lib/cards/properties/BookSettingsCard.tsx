import { Fragment } from "react"
import * as Button from '$lib/components/ui/button';
import * as Switch from '$lib/components/ui/switch';
import { infoPanelStore, type ViewerBookInfo } from '$lib/stores/infoPanel.js';
import { bookSettingsStore, type PerBookSettings } from '$lib/stores/bookSettings.js';

export function BookSettingsCard() {
  let bookInfo = $state<ViewerBookInfo | null>(null);

  let bookSettings = $state<PerBookSettings | null>(null);

  $effect(() => {
      const unsubscribe = infoPanelStore.subscribe((state) => {
        bookInfo = state.bookInfo;
      });
      return unsubscribe;
    });

  const defaultSettings: PerBookSettings = {
      favorite: false,
      rating: 0,
      readingDirection: 'left-to-right',
      doublePageView: false,
      horizontalBook: false
    };

  $effect(() => {
      if (bookInfo?.path) {
        // 获取已保存的设置，如果没有则使用默认设置
        bookSettings = bookSettingsStore.get(bookInfo.path) ?? defaultSettings;
      } else {
        bookSettings = null;
      }
    });

  function updateBookSetting(updates: Partial<PerBookSettings>) {
      if (bookInfo?.path) {
        bookSettingsStore.updateFor(bookInfo.path, updates);
        bookSettings = bookSettingsStore.get(bookInfo.path) || null;
      }
    }
  return (
    <Fragment>

<div className="space-y-2 text-xs">
	{!bookInfo ? (<Fragment>
		<div className="text-muted-foreground py-4 text-center">
			<p>未打开书籍</p>
		</div>
	</Fragment>) : (<Fragment>{!bookSettings ? (<Fragment>
		<div className="text-muted-foreground py-4 text-center">
			<p>加载中...</p>
		</div>
	</Fragment>) : (<Fragment>
		{/* TODO unsupported ConstTag */}
		<div className="flex items-center justify-between">
			<span>收藏</span>
			<Button.Root variant={bs.favorite ? 'default' : 'outline'} size="sm" className="h-7 px-3 text-xs" onClick={() => updateBookSetting({ favorite: !bs.favorite })}>
				{bs.favorite ? (<Fragment>
					已收藏
				</Fragment>) : (<Fragment>
					未收藏
				</Fragment>)}
			</Button.Root>
		</div>

		<div className="flex items-center justify-between">
			<span>评分</span>
			<div className="flex items-center gap-1">
				{[1, 2, 3, 4, 5].map((value) => (<Fragment>
					<button type="button" className={`flex h-6 w-6 items-center justify-center rounded text-[12px] ${bs.rating &&
						bs.rating >= value
							? 'text-yellow-400'
							: 'text-muted-foreground'}`} onClick={() => updateBookSetting({ rating: value })} title={'评分 ' + value + ' 星'}>
						{bs.rating && bs.rating >= value ? '★' : '☆'}
					</button>
				</Fragment>))}
			</div>
		</div>

		<div className="flex items-center justify-between">
			<span>阅读方向</span>
			<div className="flex gap-1">
				<Button.Root variant={bs.readingDirection === 'left-to-right' || !bs.readingDirection
						? 'default'
						: 'outline'} size="sm" className="h-7 px-2 text-[10px]" onClick={() => updateBookSetting({ readingDirection: 'left-to-right' })}>
					左→右
				</Button.Root>
				<Button.Root variant={bs.readingDirection === 'right-to-left' ? 'default' : 'outline'} size="sm" className="h-7 px-2 text-[10px]" onClick={() => updateBookSetting({ readingDirection: 'right-to-left' })}>
					右→左
				</Button.Root>
			</div>
		</div>

		<div className="flex items-center justify-between">
			<span>显示模式</span>
			<div className="flex gap-1">
				<Button.Root variant={!bs.doublePageView ? 'default' : 'outline'} size="sm" className="h-7 px-2 text-[10px]" onClick={() => updateBookSetting({ doublePageView: false })}>
					单页
				</Button.Root>
				<Button.Root variant={bs.doublePageView ? 'default' : 'outline'} size="sm" className="h-7 px-2 text-[10px]" onClick={() => updateBookSetting({ doublePageView: true })}>
					双页
				</Button.Root>
			</div>
		</div>

		<div className="flex items-center justify-between">
			<span>横版本子</span>
			<Switch.Root checked={bs.horizontalBook ?? false} onCheckedChange={(v) => updateBookSetting({ horizontalBook: v })} className="scale-75"></Switch.Root>
		</div>
	</Fragment>)}</Fragment>)}
</div></Fragment>
  )
}
