import { Fragment } from "react"
import { RefreshCcw, Tag } from 'lucide-react';
import * as Button from '$lib/components/ui/button';
import {
		favoriteTagStore,
		categoryColors,
		mixedGenderStore,
		cat2letter,
		createTagValue,
		type FavoriteTag
	} from '$lib/stores/emm/favoriteTagStore.js';
import { getAllUniqueManualTags, NAMESPACE_LABELS } from '$lib/stores/emm/manualTagStore.js';
import { emmTranslationStore, emmMetadataStore } from '$lib/stores/emmMetadata.js';
interface Props {
		onTagClick?: (tag: FavoriteTag, modifier?: string) => void;
		interactive?: boolean; // 是否可交互（点击标签）
	}
interface ExtendedFavoriteTag extends FavoriteTag {
		isMixedVariant?: boolean;
		originalCat?: string;
	}

export function FavoriteTagsCard(props: Props) {
  const { onTagClick, interactive = false } = props
  const genderCategories = ['female', 'male', 'mixed'];

  let isReloadingFavoriteTags = $state(false);

  function handleTagClick(tag: FavoriteTag, event: MouseEvent) {
      if (!interactive || !onTagClick) return;
      event.preventDefault();
      event.stopPropagation();
      onTagClick(tag, '');
    }

  function handleTagContextMenu(tag: FavoriteTag, event: MouseEvent) {
      if (!interactive || !onTagClick) return;
      event.preventDefault();
      event.stopPropagation();
      onTagClick(tag, '-');
    }

  function handleManualTagClick(
      mt: { namespace: string; tag: string; count: number },
      event: MouseEvent
    ) {
      if (!interactive || !onTagClick) return;
      event.preventDefault();
      event.stopPropagation();
      const translated = translateManualTag(mt.namespace, mt.tag);
      const color = getManualTagColor(mt.namespace);
      const fakeTag: FavoriteTag = {
        id: `manual:${mt.namespace}:${mt.tag}`,
        cat: mt.namespace,
        tag: mt.tag,
        letter: mt.namespace.slice(0, 1),
        display: `${mt.namespace}:${translated}`,
        value: `${mt.namespace}:"${mt.tag}"$`,
        color
      };
      onTagClick(fakeTag, '');
    }

  function handleManualTagContextMenu(
      mt: { namespace: string; tag: string; count: number },
      event: MouseEvent
    ) {
      if (!interactive || !onTagClick) return;
      event.preventDefault();
      event.stopPropagation();
      const translated = translateManualTag(mt.namespace, mt.tag);
      const color = getManualTagColor(mt.namespace);
      const fakeTag: FavoriteTag = {
        id: `manual:${mt.namespace}:${mt.tag}`,
        cat: mt.namespace,
        tag: mt.tag,
        letter: mt.namespace.slice(0, 1),
        display: `${mt.namespace}:${translated}`,
        value: `${mt.namespace}:"${mt.tag}"$`,
        color
      };
      onTagClick(fakeTag, '-');
    }

  let allManualTags = $state<Array<{ namespace: string; tag: string; count: number }>>([]);

  function refreshAllManualTags() {
      allManualTags = getAllUniqueManualTags();
    }

  refreshAllManualTags();

  function translateManualTag(namespace: string, tag: string): string {
      const dict = emmMetadataStore.getTranslationDict();
      return emmTranslationStore.translateTag(tag, namespace, dict);
    }

  function getManualTagColor(namespace: string): string {
      return categoryColors[namespace] || '#10b981';
    }

  const favoriteTagGroups = $derived.by(
      (): Array<{ name: string; tags: ExtendedFavoriteTag[] }> => {
        const baseTags = favoriteTagStore.tags;
        const isMixedEnabled = mixedGenderStore.enabled;

        if (!isMixedEnabled) {
          return favoriteTagStore.groupedTags.map((group) => ({
            name: group.name,
            tags: group.tags.map((tag) => ({ ...tag }) as ExtendedFavoriteTag)
          }));
        }

        // 开启混合时，生成变体
        const extendedTags: ExtendedFavoriteTag[] = [];
        const addedIds = new Set<string>();

        for (const tag of baseTags) {
          if (!addedIds.has(tag.id)) {
            extendedTags.push({ ...tag });
            addedIds.add(tag.id);
          }

          if (genderCategories.includes(tag.cat)) {
            for (const otherCat of genderCategories) {
              if (otherCat === tag.cat) continue;
              const variantId = `${otherCat}:${tag.tag}`;
              if (addedIds.has(variantId)) continue;

              const letter = cat2letter[otherCat] || otherCat.charAt(0);
              extendedTags.push({
                id: variantId,
                cat: otherCat,
                tag: tag.tag,
                letter,
                display: `${otherCat}:${tag.display.split(':')[1] || tag.tag}`,
                value: createTagValue(otherCat, tag.tag),
                color: tag.color,
                isMixedVariant: true,
                originalCat: tag.cat
              });
              addedIds.add(variantId);
            }
          }
        }

        // 分组
        const groups: Record<string, ExtendedFavoriteTag[]> = {};
        for (const tag of extendedTags) {
          if (!groups[tag.cat]) groups[tag.cat] = [];
          groups[tag.cat].push(tag);
        }

        return Object.entries(groups)
          .map(([name, tags]) => ({ name, tags }))
          .sort((a, b) => a.name.localeCompare(b.name));
      }
    );

  function toggleMixedGender() {
      mixedGenderStore.toggle();
    }

  async function handleReloadFavoriteTags() {
      isReloadingFavoriteTags = true;
      try {
        await favoriteTagStore.reloadFromEMM();
      } finally {
        isReloadingFavoriteTags = false;
      }
    }
  return (
    <Fragment>

<div className="space-y-2 text-[11px]">
	{/*  手动标签区域（全局唯一标签，用于搜索）  */}
	<div className="border-border/50 border-b pb-2">
		<div className="mb-1 flex items-center justify-between">
			<button type="button" className="hover:text-primary flex items-center gap-1 font-medium" onClick={refreshAllManualTags} title="点击刷新">
				<Tag className="h-3 w-3"></Tag>
				手动标签
			</button>
			<span className="text-muted-foreground text-[10px]">{allManualTags.length} 种</span>
		</div>

		{allManualTags.length === 0 ? (<Fragment>
			<p className="text-muted-foreground py-1 text-center text-[10px]">暂无手动标签</p>
			<p className="text-muted-foreground/70 text-center text-[9px]">右键文件 → 编辑标签</p>
		</Fragment>) : (<Fragment>
			<div className="flex flex-wrap gap-1">
				{allManualTags.map((mt) => (<Fragment>
					{/* TODO unsupported ConstTag */}
					{/* TODO unsupported ConstTag */}
					<button type="button" className={`inline-flex items-center gap-0.5 rounded border-2 border-dashed px-1.5 py-0.5 text-[10px] ${interactive
							? 'cursor-pointer hover:opacity-80'
							: ''}`} style={`border-color: ${color}; background: color-mix(in srgb, ${color} 12%, transparent); color: ${color};`} title={`${NAMESPACE_LABELS[mt.namespace]}: ${mt.tag}${translated !== mt.tag
							? ` (${translated})`
							: ''} (${mt.count}个文件)${interactive ? '\n左键添加，右键排除' : ''}`} onClick={(e) => handleManualTagClick(mt, e)} onContextmenu={(e) => handleManualTagContextMenu(mt, e)}>
						<span className="opacity-70">{mt.namespace.slice(0, 1)}:</span>
						<span>{translated}</span>
						<span className="text-[9px] opacity-50">×{mt.count}</span>
					</button>
				</Fragment>))}
			</div>
		</Fragment>)}
	</div>

	{/*  控制栏  */}
	<div className="border-border/50 flex items-center justify-between gap-2 border-b py-1">
		<label className="flex cursor-pointer items-center gap-1.5">
			<input type="checkbox" checked={mixedGenderStore.enabled} onChange={toggleMixedGender} className="accent-primary h-3 w-3 rounded" />
			<span className="text-muted-foreground">混合性别搜索</span>
		</label>
		<Button.Root variant="ghost" size="sm" className={`h-6 gap-1 px-2 text-[10px] ${isReloadingFavoriteTags ? 'animate-spin' : ''}`} onClick={handleReloadFavoriteTags} disabled={isReloadingFavoriteTags}>
			<RefreshCcw className="h-3 w-3"></RefreshCcw>
			刷新
		</Button.Root>
	</div>

	{/*  标签分组显示  */}
	{favoriteTagGroups.length === 0 ? (<Fragment>
		<div className="text-muted-foreground py-4 text-center">
			<p>暂无收藏标签</p>
			<p className="mt-1 text-[10px]">点击刷新从 EMM 导入</p>
		</div>
	</Fragment>) : (<Fragment>
		<div className="max-h-[300px] space-y-2 overflow-y-auto">
			{favoriteTagGroups.map((group) => (<Fragment>
				<div>
					<div className="bg-muted/50 mb-1 flex items-center justify-between rounded px-1 py-0.5">
						<span className="font-semibold" style={`color: ${categoryColors[group.name] || '#666'}`}>
							{group.name}
						</span>
						<span className="text-muted-foreground text-[10px]">{group.tags.length}</span>
					</div>
					<div className="flex flex-wrap gap-1">
						{group.tags.map((tag) => (<Fragment key={tag.id}>
							{/* TODO unsupported ConstTag */}
							{/* TODO unsupported ConstTag */}
							<button type="button" className={`inline-flex items-center gap-1 rounded border px-1.5 py-0.5 text-[10px] ${isVariant
									? 'border-dashed opacity-70'
									: ''} ${interactive ? 'cursor-pointer hover:opacity-80' : ''}`} style={`
									border-color: ${tag.color};
									background: color-mix(in srgb, ${tag.color} ${isVariant ? '5%' : '10%'}, transparent);
								`} title={isVariant
									? `混合变体 (来自 ${origCat})\n${tag.value}${interactive ? '\n左键添加，右键排除' : ''}`
									: `${tag.value}${interactive ? '\n左键添加，右键排除' : ''}`} onClick={(e) => handleTagClick(tag, e)} onContextmenu={(e) => handleTagContextMenu(tag, e)}>
								<span className={`h-1.5 w-1.5 ${isVariant ? 'rounded-sm' : 'rounded-full'}`} style={`background: ${tag.color}`}></span>
								{tag.display.split(':')[1]}
							</button>
						</Fragment>))}
					</div>
				</div>
			</Fragment>))}
		</div>
	</Fragment>)}

	<p className="text-muted-foreground border-border/50 border-t pt-1 text-[10px]">
		在文件夹面板搜索栏点击「★ 标签」使用快选
	</p>
</div></Fragment>
  )
}
