import { Fragment } from "react"
import { Button } from '$lib/components/ui/button';
import { Input } from '$lib/components/ui/input';
import { Label } from '$lib/components/ui/label';
import { aiTranslationStore } from '$lib/stores/ai/translationStore.js';
import {
		translateText,
		containsJapanese,
		detectLanguage
	} from '$lib/services/translationService';
import { Languages, ArrowRight, Loader2, CheckCircle, XCircle, Info } from 'lucide-react';

export function AiTranslationTestCard() {
  let inputText = $state('');

  let translatedText = $state('');

  let isTranslating = $state(false);

  let error = $state<string | null>(null);

  let detectedLang = $state<string | null>(null);

  let hasJapanese = $state(false);

  $effect(() => {
      if (inputText.trim()) {
        hasJapanese = containsJapanese(inputText);
        detectedLang = detectLanguage(inputText);
      } else {
        hasJapanese = false;
        detectedLang = null;
      }
    });

  async function handleTranslate() {
      if (!inputText.trim()) return;

      isTranslating = true;
      error = null;
      translatedText = '';

      try {
        const result = await translateText(inputText.trim());
        if (result.success && result.translated) {
          translatedText = result.translated;
        } else {
          error = result.error || '翻译失败';
        }
      } catch (e) {
        error = `翻译出错: ${e}`;
      } finally {
        isTranslating = false;
      }
    }

  function handleClear() {
      inputText = '';
      translatedText = '';
      error = null;
      detectedLang = null;
    }

  function getLangLabel(lang: string | null): string {
      switch (lang) {
        case 'ja':
          return '日语';
        case 'zh':
          return '中文';
        case 'en':
          return '英语';
        case 'unknown':
          return '未知';
        default:
          return '未检测';
      }
    }
  return (
    <Fragment>

<div className="space-y-4">
	{/*  输入区域  */}
	<div className="space-y-2">
		<div className="flex items-center justify-between">
			<Label className="text-xs">输入文本</Label>
			{detectedLang ? (<Fragment>
				<span className="text-muted-foreground flex items-center gap-1 text-xs">
					<Info className="h-3 w-3"></Info>
					检测: {getLangLabel(detectedLang)}
					{hasJapanese ? (<Fragment>
						<span className="text-amber-500">(含日文)</span>
					</Fragment>) : null}
				</span>
			</Fragment>) : null}
		</div>
		<Input placeholder="输入要翻译的文本，如：【かぐや様】四宮かぐや" className="text-sm"></Input>
	</div>

	{/*  翻译按钮  */}
	<div className="flex gap-2">
		<Button variant="default" size="sm" onClick={handleTranslate} disabled={!inputText.trim() || isTranslating} className="flex-1">
			{isTranslating ? (<Fragment>
				<Loader2 className="mr-2 h-4 w-4 animate-spin"></Loader2>
				翻译中...
			</Fragment>) : (<Fragment>
				<Languages className="mr-2 h-4 w-4"></Languages>
				翻译
			</Fragment>)}
		</Button>
		<Button variant="outline" size="sm" onClick={handleClear} disabled={!inputText && !translatedText}>
			清空
		</Button>
	</div>

	{/*  结果区域  */}
	{translatedText || error ? (<Fragment>
		<div className="space-y-2">
			<Label className="text-xs">翻译结果</Label>
			{error ? (<Fragment>
				<div className="flex items-start gap-2 rounded-md border border-red-500/30 bg-red-500/10 p-3 text-sm text-red-600 dark:text-red-400">
					<XCircle className="mt-0.5 h-4 w-4 shrink-0"></XCircle>
					<span>{error}</span>
				</div>
			</Fragment>) : (<Fragment>{translatedText ? (<Fragment>
				<div className="flex items-start gap-2 rounded-md border border-green-500/30 bg-green-500/10 p-3 text-sm text-green-600 dark:text-green-400">
					<CheckCircle className="mt-0.5 h-4 w-4 shrink-0"></CheckCircle>
					<span className="break-all">{translatedText}</span>
				</div>
			</Fragment>) : null}</Fragment>)}
		</div>
	</Fragment>) : null}

	{/*  示例  */}
	<div className="space-y-1">
		<Label className="text-muted-foreground text-xs">快速测试示例</Label>
		<div className="flex flex-wrap gap-1">
			{['こんにちは', '【東方】霊夢', 'お兄ちゃん大好き', '魔法少女まどか'].map((example) => (<Fragment>
				<Button variant="outline" size="sm" className="h-6 px-2 text-xs" onClick={() => {
						inputText = example;
					}}>
					{example}
				</Button>
			</Fragment>))}
		</div>
	</div>
</div></Fragment>
  )
}
