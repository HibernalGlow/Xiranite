import { Fragment } from "react"
import { invoke } from '@tauri-apps/api/core';
import CpuMonitor from './components/CpuMonitor.js';
import MemoryMonitor from './components/MemoryMonitor.js';
import IoMonitor from './components/IoMonitor.js';
import GpuMonitor from './components/GpuMonitor.js';
import { RefreshCw, Activity } from 'lucide-react';
import { Button } from '$lib/components/ui/button';
interface SystemStats {
		cpu_usage: number[];
		memory_total: number;
		memory_used: number;
		memory_free: number;
		memory_cached: number;
		uptime: number;
		load_avg: [number, number, number];
		network_rx_bytes: number;
		network_tx_bytes: number;
		disk_total_bytes: number;
		disk_used_bytes: number;
		disk_free_bytes: number;
	}

export function SystemMonitorCard() {
  const STORAGE_KEY_MONITORING = 'neoview-monitor-isMonitoring';

  const STORAGE_KEY_INTERVAL = 'neoview-monitor-refreshInterval';

  function loadFromStorage<T>(key: string, defaultValue: T): T {
      try {
        const saved = localStorage.getItem(key);
        if (saved !== null) {
          return JSON.parse(saved);
        }
      } catch (e) {
        console.error(`Failed to load ${key} from storage:`, e);
      }
      return defaultValue;
    }

  function saveToStorage<T>(key: string, value: T) {
      try {
        localStorage.setItem(key, JSON.stringify(value));
      } catch (e) {
        console.error(`Failed to save ${key} to storage:`, e);
      }
    }

  let stats = $state<SystemStats | null>(null);

  let isMonitoring = $state(loadFromStorage(STORAGE_KEY_MONITORING, true));

  let refreshInterval = $state(loadFromStorage(STORAGE_KEY_INTERVAL, 1000));

  let intervalId: number | null = null;

  let error = $state<string | null>(null);

  async function fetchSystemStats() {
      try {
        const result = await invoke<SystemStats>('get_system_stats');
        stats = result;
        error = null;
      } catch (e) {
        console.error('获取系统统计数据失败:', e);
        error = String(e);
      }
    }

  function startMonitoring() {
      if (isMonitoring) return;

      isMonitoring = true;
      fetchSystemStats(); // 立即获取一次

      intervalId = window.setInterval(() => {
        fetchSystemStats();
      }, refreshInterval);
    }

  function stopMonitoring() {
      if (!isMonitoring) return;

      isMonitoring = false;
      if (intervalId !== null) {
        clearInterval(intervalId);
        intervalId = null;
      }
    }

  function toggleMonitoring() {
      if (isMonitoring) {
        stopMonitoring();
      } else {
        startMonitoring();
      }
      // 保存监控状态
      saveToStorage(STORAGE_KEY_MONITORING, isMonitoring);
    }

  async function handleRefresh() {
      await fetchSystemStats();
    }

  onMount(() => {
      // 如果保存的状态是开启监控，则启动监控
      if (isMonitoring) {
        startMonitoring();
      }
    });

  onDestroy(() => {
      stopMonitoring();
    });

  function formatUptime(seconds: number): string {
      const days = Math.floor(seconds / 86400);
      const hours = Math.floor((seconds % 86400) / 3600);
      const minutes = Math.floor((seconds % 3600) / 60);

      if (days > 0) return `${days}天 ${hours}小时`;
      if (hours > 0) return `${hours}小时 ${minutes}分钟`;
      return `${minutes}分钟`;
    }
  return (
    <Fragment>

<div className="space-y-3">
	{/*  控制栏  */}
	<div className="flex items-center justify-between gap-2">
		<div className="flex items-center gap-2">
			<Button variant={isMonitoring ? 'destructive' : 'default'} size="sm" onClick={toggleMonitoring}>
				<Activity className="mr-1 h-4 w-4"></Activity>
				{isMonitoring ? '停止监控' : '开始监控'}
			</Button>
			<Button variant="outline" size="sm" onClick={handleRefresh} disabled={!stats}>
				<RefreshCw className="mr-1 h-4 w-4"></RefreshCw>
				刷新
			</Button>
		</div>

		{/*  刷新间隔选择  */}
		<div className="flex items-center gap-2">
			<span className="text-muted-foreground text-xs">刷新间隔:</span>
			<select className="bg-background rounded border px-2 py-1 text-xs" onChange={() => {
					// 保存刷新间隔
					saveToStorage(STORAGE_KEY_INTERVAL, refreshInterval);
					if (isMonitoring) {
						stopMonitoring();
						startMonitoring();
					}
				}}>
				<option value={500}>0.5秒</option>
				<option value={1000}>1秒</option>
				<option value={2000}>2秒</option>
				<option value={5000}>5秒</option>
			</select>
		</div>
	</div>

	{/*  错误提示  */}
	{error ? (<Fragment>
		<div className="bg-destructive/10 text-destructive rounded p-2 text-xs">
			{error}
		</div>
	</Fragment>) : null}

	{/*  系统信息总览  */}
	{stats ? (<Fragment>
		<div className="grid grid-cols-3 gap-2 text-xs">
			<div className="bg-muted/50 rounded p-2">
				<div className="text-muted-foreground mb-1">系统运行时间</div>
				<div className="font-mono font-semibold">{formatUptime(stats.uptime)}</div>
			</div>
			<div className="bg-muted/50 rounded p-2">
				<div className="text-muted-foreground mb-1">负载平均</div>
				<div className="font-mono text-[10px]">
					{stats.load_avg[0].toFixed(2)} / {stats.load_avg[1].toFixed(2)} / {stats.load_avg[2].toFixed(
						2
					)}
				</div>
			</div>
			<div className="bg-muted/50 rounded p-2">
				<div className="text-muted-foreground mb-1">CPU 核心</div>
				<div className="font-mono font-semibold">{stats.cpu_usage.length} 核</div>
			</div>
		</div>

		{/*  CPU 监控  */}
		<CpuMonitor cpuUsage={stats.cpu_usage}></CpuMonitor>

		{/*  内存监控  */}
		<MemoryMonitor memoryTotal={stats.memory_total} memoryUsed={stats.memory_used} memoryFree={stats.memory_free} memoryCached={stats.memory_cached}></MemoryMonitor>

		{/*  IO 监控  */}
		<IoMonitor networkRx={stats.network_rx_bytes} networkTx={stats.network_tx_bytes} diskTotal={stats.disk_total_bytes} diskUsed={stats.disk_used_bytes} diskFree={stats.disk_free_bytes}></IoMonitor>

		{/*  GPU 监控 (需要后端 sysinfo 扩展支持)  */}
		<GpuMonitor></GpuMonitor>
	</Fragment>) : (<Fragment>
		<div className="text-muted-foreground py-8 text-center">
			{isMonitoring ? '加载中...' : '点击"开始监控"查看系统状态'}
		</div>
	</Fragment>)}
</div></Fragment>
  )
}
