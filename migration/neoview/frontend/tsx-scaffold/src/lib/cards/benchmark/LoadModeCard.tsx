import { Fragment } from "react"
import { invoke } from '@tauri-apps/api/core';
import { open } from '@tauri-apps/plugin-dialog';
import { Button } from '$lib/components/ui/button';
import { FolderOpen, Play, Trash2 } from 'lucide-react';
interface LoadModeTestResult {
		mode: string;
		format: string;
		input_size: number;
		output_size: number;
		decode_ms: number;
		width: number | null;
		height: number | null;
		success: boolean;
		error: string | null;
	}

export function LoadModeCard() {
  let selectedLoadModeFile = $state<string>('');

  let loadModeResults = $state<LoadModeTestResult[]>([]);

  let isRunning = $state(false);

  async function selectLoadModeFile() {
      const file = await open({
        multiple: false,
        filters: [
          { name: '图像', extensions: ['jpg', 'jpeg', 'png', 'webp', 'avif', 'jxl', 'gif', 'bmp'] }
        ]
      });

      if (file && typeof file === 'string') {
        selectedLoadModeFile = file;
      }
    }

  async function runLoadModeTest() {
      if (!selectedLoadModeFile) return;

      isRunning = true;
      loadModeResults = [];

      try {
        // 后端测试
        const backendResults = await invoke<LoadModeTestResult[]>('test_load_modes', {
          filePath: selectedLoadModeFile
        });

        // 前端测试
        const frontendResults: LoadModeTestResult[] = [];

        // Raw 模式测试
        try {
          const startTotal = performance.now();
          const rawData = await invoke<number[]>('load_image', { path: selectedLoadModeFile });

          const blob = new Blob([new Uint8Array(rawData)]);
          const url = URL.createObjectURL(blob);

          const imgSize = await new Promise<{ w: number; h: number }>((resolve, reject) => {
            const img = new Image();
            img.onload = () => {
              const size = { w: img.naturalWidth, h: img.naturalHeight };
              URL.revokeObjectURL(url);
              resolve(size);
            };
            img.onerror = () => reject(new Error('图片加载失败'));
            img.src = url;
          });
          const totalTime = performance.now() - startTotal;

          frontendResults.push({
            mode: 'Raw→Blob→img (完整)',
            format: selectedLoadModeFile.split('.').pop() || '',
            input_size: rawData.length,
            output_size: rawData.length,
            decode_ms: totalTime,
            width: imgSize.w,
            height: imgSize.h,
            success: true,
            error: null
          });
        } catch (e) {
          frontendResults.push({
            mode: 'Raw→Blob→img (完整)',
            format: selectedLoadModeFile.split('.').pop() || '',
            input_size: 0,
            output_size: 0,
            decode_ms: 0,
            width: null,
            height: null,
            success: false,
            error: String(e)
          });
        }

        loadModeResults = [...backendResults, ...frontendResults];
      } catch (err) {
        console.error('加载模式测试失败:', err);
      } finally {
        isRunning = false;
      }
    }

  function clearLoadModeFile() {
      selectedLoadModeFile = '';
      loadModeResults = [];
    }

  function formatFileSize(bytes: number): string {
      if (bytes < 1024) return `${bytes} B`;
      if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
      return `${(bytes / (1024 * 1024)).toFixed(2)} MB`;
    }
  return (
    <Fragment>

<div className="space-y-2">
	<p className="text-muted-foreground text-[10px]">对比 Raw/Bitmap/缩放 等加载模式性能</p>
	<div className="flex gap-2">
		<Button onClick={selectLoadModeFile} variant="outline" size="sm" className="flex-1 text-xs">
			<FolderOpen className="mr-1 h-3 w-3"></FolderOpen>
			{selectedLoadModeFile ? '已选择' : '选择图像'}
		</Button>
		<Button onClick={runLoadModeTest} disabled={isRunning || !selectedLoadModeFile} size="sm" className="flex-1 text-xs">
			<Play className="mr-1 h-3 w-3"></Play>
			{isRunning ? '测试中...' : '测试加载'}
		</Button>
		{selectedLoadModeFile ? (<Fragment>
			<Button onClick={clearLoadModeFile} variant="ghost" size="sm" className="text-xs">
				<Trash2 className="h-3 w-3"></Trash2>
			</Button>
		</Fragment>) : null}
	</div>

	{selectedLoadModeFile ? (<Fragment>
		<div className="text-muted-foreground truncate text-[10px]">
			{selectedLoadModeFile.split(/[/\\]/).pop()}
		</div>
	</Fragment>) : null}

	{loadModeResults.length > 0 ? (<Fragment>
		<div className="space-y-2 text-[10px]">
			{loadModeResults.map((result) => (<Fragment>
				<div className="space-y-1 rounded border p-2">
					<div className="flex justify-between font-medium">
						<span>
							{result.mode}
						</span>
						<span className={`font-mono ${result.success ? 'text-green-600' : 'text-red-500'}`}>
							{result.success ? `${result.decode_ms.toFixed(1)}ms` : '失败'}
						</span>
					</div>
					{result.success ? (<Fragment>
						<div className="text-muted-foreground mt-1 flex justify-between">
							<span>输入: {formatFileSize(result.input_size)}</span>
							<span>输出: {formatFileSize(result.output_size)}</span>
						</div>
						{result.width && result.height ? (<Fragment>
							<div className="text-muted-foreground">
								尺寸: {result.width}×{result.height}
							</div>
						</Fragment>) : null}
					</Fragment>) : (<Fragment>{result.error ? (<Fragment>
						<div className="text-[9px] text-red-500">{result.error}</div>
					</Fragment>) : null}</Fragment>)}
				</div>
			</Fragment>))}
		</div>
	</Fragment>) : null}
</div></Fragment>
  )
}
