import { Fragment } from "react"
import { invoke } from '@tauri-apps/api/core';
import { open } from '@tauri-apps/plugin-dialog';
import { Button } from '$lib/components/ui/button';
import { FolderOpen, Play, Trash2 } from 'lucide-react';
import { settingsManager } from '$lib/settings/settingsManager';
import Viewer from 'viewerjs';
interface RendererTestResult {
		mode: string;
		totalImages: number;
		loadTimes: number[];
		avgLoadTime: number;
		switchTimes: number[];
		avgSwitchTime: number;
		fps: number;
		success: boolean;
		error: string | null;
	}

export function RendererCard() {
  let selectedRendererArchive = $state<string>('');

  let rendererTestResults = $state<RendererTestResult[]>([]);

  let isRendererTesting = $state(false);

  let rendererTestCount = $state<number>(10);

  let settings = $state(settingsManager.getSettings());

  settingsManager.addListener((s) => {
      settings = s;
    });

  const currentRendererMode = $derived(settings.view.renderer?.mode ?? 'standard');

  const viewerJSEnabled = $derived(settings.view.renderer?.useViewerJS ?? false);

  async function selectRendererArchive() {
      const file = await open({
        multiple: false,
        filters: [{ name: '压缩包', extensions: ['zip', 'cbz', 'rar', '7z', 'cb7', 'cbr'] }]
      });

      if (file && typeof file === 'string') {
        selectedRendererArchive = file;
      }
    }

  async function runRendererTest() {
      if (!selectedRendererArchive) return;

      isRendererTesting = true;
      rendererTestResults = [];

      const testModes = ['native', 'viewerjs'];

      try {
        const imageList = await invoke<string[]>('get_images_from_archive', {
          archivePath: selectedRendererArchive
        });

        if (imageList.length === 0) {
          for (const mode of testModes) {
            rendererTestResults = [
              ...rendererTestResults,
              {
                mode,
                totalImages: 0,
                loadTimes: [],
                avgLoadTime: 0,
                switchTimes: [],
                avgSwitchTime: 0,
                fps: 0,
                success: false,
                error: '压缩包中没有图片'
              }
            ];
          }
          isRendererTesting = false;
          return;
        }

        const testImages = imageList.slice(0, rendererTestCount);

        const testContainer = document.createElement('div');
        testContainer.style.cssText =
          'position:fixed;left:-9999px;top:0;width:800px;height:600px;overflow:hidden;';
        document.body.appendChild(testContainer);

        for (const mode of testModes) {
          try {
            const loadTimes: number[] = [];

            for (let i = 0; i < testImages.length; i++) {
              const startLoad = performance.now();

              const imageData = await invoke<number[]>('load_image_from_archive', {
                archivePath: selectedRendererArchive,
                filePath: testImages[i]
              });

              const blob = new Blob([new Uint8Array(imageData)]);
              const url = URL.createObjectURL(blob);

              if (mode === 'viewerjs') {
                await new Promise<void>((resolve, reject) => {
                  testContainer.innerHTML = '';
                  const img = document.createElement('img');
                  img.style.display = 'none';
                  img.src = url;
                  testContainer.appendChild(img);

                  img.onload = () => {
                    try {
                      const viewer = new Viewer(img, {
                        inline: true,
                        navbar: false,
                        toolbar: false,
                        title: false,
                        button: false,
                        backdrop: false,
                        transition: false,
                        container: testContainer,
                        ready: () => {
                          viewer.destroy();
                          URL.revokeObjectURL(url);
                          resolve();
                        }
                      });
                      viewer.show();
                    } catch (e) {
                      URL.revokeObjectURL(url);
                      reject(e);
                    }
                  };
                  img.onerror = () => {
                    URL.revokeObjectURL(url);
                    reject(new Error('图片加载失败'));
                  };
                });
              } else {
                await new Promise<void>((resolve, reject) => {
                  testContainer.innerHTML = '';
                  const img = document.createElement('img');
                  img.style.cssText = 'max-width:100%;max-height:100%;object-fit:contain;';
                  testContainer.appendChild(img);

                  img.onload = () => {
                    URL.revokeObjectURL(url);
                    resolve();
                  };
                  img.onerror = () => {
                    URL.revokeObjectURL(url);
                    reject(new Error('图片加载失败'));
                  };
                  img.src = url;
                });
              }

              loadTimes.push(performance.now() - startLoad);
            }

            const avgLoadTime = loadTimes.reduce((a, b) => a + b, 0) / loadTimes.length;
            const fps = 1000 / avgLoadTime;

            rendererTestResults = [
              ...rendererTestResults,
              {
                mode,
                totalImages: testImages.length,
                loadTimes,
                avgLoadTime,
                switchTimes: [],
                avgSwitchTime: 0,
                fps,
                success: true,
                error: null
              }
            ];
          } catch (err) {
            rendererTestResults = [
              ...rendererTestResults,
              {
                mode,
                totalImages: 0,
                loadTimes: [],
                avgLoadTime: 0,
                switchTimes: [],
                avgSwitchTime: 0,
                fps: 0,
                success: false,
                error: String(err)
              }
            ];
          }
        }

        testContainer.remove();
      } catch (err) {
        console.error('渲染模式测试失败:', err);
      }

      isRendererTesting = false;
    }

  function clearRendererArchive() {
      selectedRendererArchive = '';
      rendererTestResults = [];
    }
  return (
    <Fragment>

<div className="space-y-2">
	<div className="text-muted-foreground space-y-1 text-[10px]">
		<p>当前模式: <span className="text-primary font-mono">{currentRendererMode}</span></p>
		<p>
			ViewerJS: <span className={`font-mono ${viewerJSEnabled ? 'text-green-500' : 'text-red-500'}`}>{viewerJSEnabled ? '启用' : '禁用'}</span>
		</p>
	</div>

	<div className="flex gap-2">
		<Button onClick={selectRendererArchive} variant="outline" size="sm" className="flex-1 text-xs">
			<FolderOpen className="mr-1 h-3 w-3"></FolderOpen>
			{selectedRendererArchive ? '已选择' : '选择压缩包'}
		</Button>
		<Button onClick={runRendererTest} disabled={isRendererTesting || !selectedRendererArchive} size="sm" className="flex-1 text-xs">
			<Play className="mr-1 h-3 w-3"></Play>
			{isRendererTesting ? '测试中...' : '测试渲染'}
		</Button>
		{selectedRendererArchive ? (<Fragment>
			<Button onClick={clearRendererArchive} variant="ghost" size="sm" className="text-xs">
				<Trash2 className="h-3 w-3"></Trash2>
			</Button>
		</Fragment>) : null}
	</div>

	{selectedRendererArchive ? (<Fragment>
		<div className="text-muted-foreground truncate text-[10px]">
			{selectedRendererArchive.split(/[/\\]/).pop()}
		</div>
	</Fragment>) : null}

	{/*  测试数量  */}
	<div className="flex items-center gap-2 text-[10px]">
		<span className="text-muted-foreground">图片数:</span>
		<div className="flex gap-1">
			{[5, 10, 20, 50].map((count) => (<Fragment>
				<button type="button" className={`rounded px-2 py-0.5 ${rendererTestCount === count
						? 'bg-primary text-primary-foreground'
						: 'bg-muted hover:bg-muted/80'}`} onClick={() => (rendererTestCount = count)}>
					{count}
				</button>
			</Fragment>))}
		</div>
	</div>

	{/*  结果  */}
	{rendererTestResults.length > 0 ? (<Fragment>
		<div className="space-y-2 text-[10px]">
			{rendererTestResults.map((result) => (<Fragment>
				<div className="space-y-1 rounded border p-2">
					<div className="flex justify-between font-medium">
						<span>
							{result.mode === 'native' ? '原生渲染' : 'ViewerJS'}
						</span>
						<span className={`font-mono ${result.success ? 'text-green-600' : 'text-red-500'}`}>
							{result.success ? `${result.avgLoadTime.toFixed(1)}ms` : '失败'}
						</span>
					</div>
					{result.success ? (<Fragment>
						<div className="text-muted-foreground">
							FPS: <span className="text-primary font-mono">{result.fps.toFixed(1)}</span>
						</div>
					</Fragment>) : (<Fragment>{result.error ? (<Fragment>
						<div className="text-[9px] text-red-500">{result.error}</div>
					</Fragment>) : null}</Fragment>)}
				</div>
			</Fragment>))}
		</div>
	</Fragment>) : null}
</div></Fragment>
  )
}
