import { Fragment } from "react"
import { Button } from '$lib/components/ui/button';
import { aiTranslationStore } from '$lib/stores/ai/translationStore.js';
import { Database, Trash2, Download, Upload, RefreshCcw, HardDrive } from 'lucide-react';
import { invoke } from '@tauri-apps/api/core';
import { confirm } from '$lib/stores/confirmDialog.js';
import { showSuccessToast, showErrorToast } from '$lib/utils/toast';

export function AiTranslationCacheCard() {
  let stats = $state(aiTranslationStore.getCacheStats());

  let dbCacheCount = $state<number | null>(null);

  async function loadDbCacheStats() {
      try {
        const count = await invoke<number>('get_ai_translation_count');
        dbCacheCount = count;
      } catch {
        dbCacheCount = null;
      }
    }

  onMount(() => {
      loadDbCacheStats();
    });

  $effect(() => {
      const unsubscribe = aiTranslationStore.subscribe((state) => {
        stats = {
          size: state.cache.size,
          ...state.stats
        };
      });
      return unsubscribe;
    });

  async function handleClearCache() {
      const confirmed = await confirm({
        title: '确认清空',
        description: '确定要清空所有翻译缓存吗？',
        confirmText: '清空',
        cancelText: '取消',
        variant: 'destructive'
      });
      if (confirmed) {
        aiTranslationStore.clearCache();
      }
    }

  function handleExportCache() {
      const entries = aiTranslationStore.exportCache();
      const json = JSON.stringify(entries, null, 2);
      const blob = new Blob([json], { type: 'application/json' });
      const url = URL.createObjectURL(blob);

      const a = document.createElement('a');
      a.href = url;
      a.download = `neoview-ai-translation-cache-${Date.now()}.json`;
      document.body.appendChild(a);
      a.click();
      document.body.removeChild(a);
      URL.revokeObjectURL(url);
    }

  function handleImportCache() {
      const input = document.createElement('input');
      input.type = 'file';
      input.accept = 'application/json,.json';
      input.onchange = async (e) => {
        const file = (e.target as HTMLInputElement)?.files?.[0];
        if (!file) return;

        try {
          const text = await file.text();
          const entries = JSON.parse(text);
          if (Array.isArray(entries)) {
            aiTranslationStore.importCache(entries);
            showSuccessToast('导入成功', `成功导入 ${entries.length} 条翻译缓存`);
          }
        } catch (err) {
          showErrorToast('导入失败', '请检查文件格式');
        }
      };
      input.click();
    }

  function formatNumber(n: number): string {
      return n.toLocaleString();
    }

  function formatCacheHitRate(): string {
      const total = stats.cacheHits + stats.apiCalls;
      if (total === 0) return '0%';
      return ((stats.cacheHits / total) * 100).toFixed(1) + '%';
    }
  return (
    <Fragment>

<div className="space-y-4">
	{/*  缓存统计  */}
	<div className="grid grid-cols-2 gap-2">
		<div className="bg-muted/20 rounded-md border p-3 text-center">
			<div className="text-primary text-2xl font-bold">{formatNumber(stats.size)}</div>
			<div className="text-muted-foreground text-xs">缓存条目</div>
		</div>
		<div className="bg-muted/20 rounded-md border p-3 text-center">
			<div className="text-primary text-2xl font-bold">{formatCacheHitRate()}</div>
			<div className="text-muted-foreground text-xs">缓存命中率</div>
		</div>
	</div>

	<div className="space-y-1 text-sm">
		<div className="flex justify-between">
			<span className="text-muted-foreground">总翻译数:</span>
			<span>{formatNumber(stats.totalTranslations)}</span>
		</div>
		<div className="flex justify-between">
			<span className="text-muted-foreground">缓存命中:</span>
			<span>{formatNumber(stats.cacheHits)}</span>
		</div>
		<div className="flex justify-between">
			<span className="text-muted-foreground">API 调用:</span>
			<span>{formatNumber(stats.apiCalls)}</span>
		</div>
		<div className="flex items-center justify-between">
			<span className="text-muted-foreground flex items-center gap-1">
				<HardDrive className="h-3 w-3"></HardDrive>
				数据库缓存:
			</span>
			<span className="flex items-center gap-1">
				{dbCacheCount !== null ? formatNumber(dbCacheCount) : '加载中...'}
				<button className="hover:bg-muted rounded p-0.5" onClick={() => loadDbCacheStats()} title="刷新数据库缓存数量">
					<RefreshCcw className="text-muted-foreground h-3 w-3"></RefreshCcw>
				</button>
			</span>
		</div>
	</div>

	{/*  操作按钮  */}
	<div className="flex flex-wrap gap-2">
		<Button variant="outline" size="sm" onClick={handleExportCache} className="flex-1">
			<Download className="mr-1 h-3 w-3"></Download>
			导出
		</Button>
		<Button variant="outline" size="sm" onClick={handleImportCache} className="flex-1">
			<Upload className="mr-1 h-3 w-3"></Upload>
			导入
		</Button>
		<Button variant="outline" size="sm" onClick={handleClearCache} className="text-destructive hover:text-destructive flex-1">
			<Trash2 className="mr-1 h-3 w-3"></Trash2>
			清空
		</Button>
	</div>
</div></Fragment>
  )
}
