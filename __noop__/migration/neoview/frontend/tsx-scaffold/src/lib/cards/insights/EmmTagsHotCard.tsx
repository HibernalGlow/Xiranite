import { Fragment } from "react"
import { emmMetadataStore } from '$lib/stores/emmMetadata.js';
import { Badge } from '$lib/components/ui/badge';
import type { EMMCollectTag } from '$lib/api/emm';

export function EmmTagsHotCard() {
  let collectTags = $state<EMMCollectTag[]>([]);

  $effect(() => {
      const unsubscribe = emmMetadataStore.subscribe((value) => {
        collectTags = value.collectTags ?? [];
      });
      return unsubscribe;
    });

  function buildEmmTagStats() {
      const total = collectTags.length;
      const byLetter: Record<string, number> = {};
      for (const tag of collectTags) {
        const letter = tag.letter || 'other';
        byLetter[letter] = (byLetter[letter] ?? 0) + 1;
      }
      const topLetters = Object.entries(byLetter)
        .sort((a, b) => b[1] - a[1])
        .slice(0, 5);
      const previewTags = collectTags.slice(0, 6);
      return { total, topLetters, previewTags };
    }

  const stats = $derived(buildEmmTagStats());
  return (
    <Fragment>

<div className="space-y-3">
	<div className="flex items-center justify-between text-xs">
		<span className="text-muted-foreground">收藏标签总数</span>
		<span className="font-semibold">{stats.total}</span>
	</div>

	{stats.topLetters.length > 0 ? (<Fragment>
		<div className="flex flex-wrap gap-1.5">
			{stats.topLetters.map(([letter, count]) => (<Fragment>
				<Badge variant="secondary" className="text-[10px]">
					{letter}: {count}
				</Badge>
			</Fragment>))}
		</div>
	</Fragment>) : null}

	{stats.previewTags.length > 0 ? (<Fragment>
		<div className="flex flex-wrap gap-1">
			{stats.previewTags.map((tag) => (<Fragment>
				<span className="bg-muted/50 text-muted-foreground rounded px-1.5 py-0.5 text-[10px]">
					{tag.tag}
				</span>
			</Fragment>))}
			{stats.total > 6 ? (<Fragment>
				<span className="text-muted-foreground text-[10px]">+{stats.total - 6} 更多</span>
			</Fragment>) : null}
		</div>
	</Fragment>) : (<Fragment>
		<p className="text-muted-foreground py-4 text-center text-xs">暂无 EMM 标签数据</p>
	</Fragment>)}
</div></Fragment>
  )
}
