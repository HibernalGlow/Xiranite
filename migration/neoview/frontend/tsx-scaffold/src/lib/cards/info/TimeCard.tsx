import { Fragment } from "react"
import { infoPanelStore, type ViewerImageInfo } from '$lib/stores/infoPanel.js';

export function TimeCard() {
  let imageInfo = $state<ViewerImageInfo | null>(null);

  $effect(() => {
      const unsubscribe = infoPanelStore.subscribe((state) => {
        imageInfo = state.imageInfo;
      });
      return unsubscribe;
    });

  function formatDate(date?: string): string {
      if (!date) return '—';
      const parsed = new Date(date);
      if (Number.isNaN(parsed.getTime())) return date;
      return parsed.toLocaleString('zh-CN');
    }
  return (
    <Fragment>

{imageInfo ? (<Fragment>
	<div className="space-y-2 text-sm">
		<div className="flex justify-between">
			<span className="text-muted-foreground">创建时间:</span>
			<span className="text-xs">{formatDate(imageInfo.createdAt)}</span>
		</div>
		<div className="flex justify-between">
			<span className="text-muted-foreground">修改时间:</span>
			<span className="text-xs">{formatDate(imageInfo.modifiedAt)}</span>
		</div>
	</div>
</Fragment>) : (<Fragment>
	<div className="text-muted-foreground py-2 text-center text-sm">暂无时间信息</div>
</Fragment>)}</Fragment>
  )
}
